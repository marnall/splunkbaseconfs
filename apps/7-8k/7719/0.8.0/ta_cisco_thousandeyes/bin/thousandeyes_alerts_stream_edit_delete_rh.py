import json
import os
import traceback

import import_declare_test  # noqa: F401
import splunk.rest as rest
from log_helper import setup_logging
from splunktaucclib.rest_handler.admin_external import AdminExternalHandler
from splunktaucclib.rest_handler.error import RestError
from thousandeyes_client import ThousandEyesClient
from thousandeyes_constant import (
    THOUSANDEYES_TA_NAME,
    THOUSANDEYES_WEBHOOK_PAYLOAD_TEMPLATE,
)  # noqa E402
from thousandeyes_utils import get_account_id, get_single_hec_token
from thousandeyes_validator_alerts_stream import AlertsStreamValidator

logger = setup_logging(os.path.splitext(os.path.basename(__file__))[0].lower())


class AlertsStreamInputHandler(AdminExternalHandler):
    """
    Manage the Rest Handler for Alerts Stream Input.

    :param AdminExternalHandler: inhereting AdminExternalHandler
    """

    def __init__(self, *args, **kwargs):
        """
        Initialize Rest Handler.

        :param args: positional arguments.
        :param kwargs: key value arguments.

        :return: AlertsStreamInputHandler Object
        """
        AdminExternalHandler.__init__(self, *args, **kwargs)

    def handleCreate(self, conf_info):
        """
        Handle the create operation.

        :param conf_info: Default parameter generated by UCC which is used to pass the response.
        """
        try:
            logger.info(
                f"Creating the ThousandEyes Alerts Stream input: {self.callerArgs.id}."
            )
            alerts_validator = AlertsStreamValidator(self.getSessionKey(), logger)
            alerts_validator.validate(self.payload)

            # Create Splunk configuration first to fail fast if name already exists
            super().handleCreate(conf_info)

            # Setup webhook integration after Splunk config is created
            try:
                self._setup_webhook_integration()
                
                # Update Splunk config with webhook IDs
                self._update_splunk_config_with_webhook_ids()
                
            except Exception as webhook_error:
                # If webhook setup fails, roll back the Splunk configuration
                logger.error(
                    f"Webhook setup failed for {self.callerArgs.id}. Rolling back Splunk configuration."
                )
                try:
                    super().handleRemove(conf_info)
                except Exception:
                    pass  # Ignore errors during rollback
                raise webhook_error

            logger.info(
                f"Successfully created a ThousandEyes alert stream input: {self.callerArgs.id}."
            )
        except Exception as e:
            logger.error(
                f"Error occurred while creating a ThousandEyes alert stream input: {self.callerArgs.id}."
                f"Error: {e} {traceback.format_exc()}"
            )
            raise RestError(
                409,
                f"Error while configuring ThousandEyes alert stream input. Error: {e}. Please check the logs.",
            )

    def handleEdit(self, conf_info):
        """
        Handle the edit operation.

        :param conf_info: Default parameter generated by UCC which is used to pass the response
        """
        try:
            logger.info(
                f"Updating the ThousandEyes Alerts Stream input: {self.callerArgs.id}."
            )
            alerts_validator = AlertsStreamValidator(self.getSessionKey(), logger)
            alerts_validator.validate(self.payload)

            session_key = self.getSessionKey()
            _, response_content = rest.simpleRequest(
                f"/servicesNS/nobody/{THOUSANDEYES_TA_NAME}/data/inputs/alerts_stream/{self.callerArgs.id}",
                sessionKey=session_key,
                method="GET",
                getargs={"output_mode": "json"},
                raiseAllErrors=True,
            )
            response_content = json.loads(response_content)["entry"][0]["content"]
            # Ensure webhook IDs are populated from stored config if not provided or empty
            if not self.payload.get("webhook_operation_id"):
                self.payload["webhook_operation_id"] = response_content.get("webhook_operation_id")
            if not self.payload.get("webhook_connector_id"):
                self.payload["webhook_connector_id"] = response_content.get("webhook_connector_id")

            # Check if disabled status changed - need to enable/disable webhook in ThousandEyes
            old_disabled = response_content.get("disabled", "0")
            new_disabled = self.payload.get("disabled", "0")
            # Normalize values (could be "0"/"1", 0/1, or "true"/"false")
            old_disabled_bool = str(old_disabled).lower() in ("1", "true")
            new_disabled_bool = str(new_disabled).lower() in ("1", "true")
            
            if old_disabled_bool != new_disabled_bool:
                webhook_operation_id = self.payload.get("webhook_operation_id")
                if webhook_operation_id:
                    logger.info(f"Input status changed. Disabled: {old_disabled_bool} -> {new_disabled_bool}")
                    self._update_webhook_enabled_status(
                        response_content.get("thousandeyes_user"),
                        response_content.get("thousandeyes_acc_group"),
                        webhook_operation_id,
                        enabled=not new_disabled_bool
                    )

            # Setup webhook integration only if relevant fields changed
            webhook_fields = ["alert_rules", "hec_target", "hec_token", "alerts_index"]
            webhook_changed = self._has_webhook_config_changed(response_content, webhook_fields)

            if webhook_changed:
                logger.info("Webhook configuration changed, updating webhook integration")
                # Pass old alert rules to handle removed rules
                old_alert_rules = response_content.get("alert_rules", "")
                self._setup_webhook_integration(old_alert_rules=old_alert_rules)
            else:
                logger.info("No webhook configuration changes detected, skipping webhook update")

            logger.info(
                f"Successfully updated the ThousandEyes alerts stream: {self.callerArgs.id}."
            )
        except Exception as e:
            logger.error(
                f"Error occurred while editing the ThousandEyes alerts stream: {self.callerArgs.id}. "
                f"Error: {e}"
            )
            raise RestError(
                409,
                f"Error occurred while editing the ThousandEyes alert stream input. Error: {e}. Please check the logs.",
            )

        super().handleEdit(conf_info)

    def _update_splunk_config_with_webhook_ids(self):
        """Update Splunk configuration with webhook operation and connector IDs"""
        # Build update payload with only webhook IDs that are present
        update_payload = {
            key: self.payload[key]
            for key in ("webhook_operation_id", "webhook_connector_id")
            if key in self.payload
        }

        if not update_payload:
            logger.debug("No webhook IDs to update in Splunk config, skipping")
            return

        session_key = self.getSessionKey()
        
        # Update the Splunk configuration
        rest.simpleRequest(
            f"/servicesNS/nobody/{THOUSANDEYES_TA_NAME}/data/inputs/alerts_stream/{self.callerArgs.id}",
            sessionKey=session_key,
            method="POST",
            postargs=update_payload,
            raiseAllErrors=True,
        )
        logger.debug(f"Updated Splunk config with webhook IDs: {update_payload}")

    def _has_webhook_config_changed(self, old_config, fields):
        """Check if any webhook-related fields have changed
        
        :param old_config: Previous configuration dict
        :param fields: List of field names to check
        :return: True if any field changed, False otherwise
        """
        def field_changed(field):
            if field not in self.payload:
                return False
            old_val = str(old_config.get(field) or "")
            new_val = str(self.payload.get(field) or "")
            if old_val != new_val:
                logger.info(f"Webhook field '{field}' changed. Old: '{old_val}', New: '{new_val}'")
                return True
            return False

        return any(field_changed(field) for field in fields)

    def _update_webhook_enabled_status(self, thousandeyes_user, thousandeyes_acc_group, webhook_operation_id, enabled):
        """Update the enabled status of the webhook operation in ThousandEyes
        
        :param thousandeyes_user: ThousandEyes user email
        :param thousandeyes_acc_group: ThousandEyes account group
        :param webhook_operation_id: The webhook operation ID to update
        :param enabled: Boolean - True to enable, False to disable
        """
        if not thousandeyes_user or not webhook_operation_id:
            logger.warning("Cannot update webhook status: missing user or operation ID")
            return
            
        session_key = self.getSessionKey()
        aid = get_account_id(thousandeyes_acc_group)
        
        thousandeyes_client = ThousandEyesClient(
            session_key, thousandeyes_user, logger
        )
        
        try:
            # Get current operation details
            current_operation = thousandeyes_client.get_webhook_operation(webhook_operation_id, aid)
            
            # Update only the enabled field
            update_payload = {
                "name": current_operation.get("name"),
                "status": current_operation.get("status", "connected"),
                "category": current_operation.get("category", "alerts"),
                "type": current_operation.get("type", "webhook"),
                "payload": current_operation.get("payload", ""),
                "headers": current_operation.get("headers", []),
                "enabled": enabled
            }
            
            thousandeyes_client.update_webhook_operation(webhook_operation_id, update_payload, aid)
            status_str = "enabled" if enabled else "disabled"
            logger.info(f"Successfully {status_str} webhook operation {webhook_operation_id}")
            
        except Exception as e:
            logger.error(f"Failed to update webhook enabled status: {str(e)}")
            raise Exception(f"Failed to update webhook status in ThousandEyes: {str(e)}")

    def handleRemove(self, conf_info):
        """
        Handle the delete operation.

        :param conf_info: Default parameter generated by UCC which is used to pass the response
        """
        try:
            logger.info(
                f"Deleting the ThousandEyes Alerts Stream input: {self.callerArgs.id}."
            )

            # Clean up webhook integration
            self._cleanup_webhook_integration()

            super().handleRemove(conf_info)
            logger.info(
                f"Successfully deleted the ThousandEyes alerts stream input: {self.callerArgs.id}."
            )
        except Exception as e:
            logger.error(
                f"Error occurred while deleting the ThousandEyes alert stream input: {self.callerArgs.id}. "
                f"Error: {e}"
            )
            raise RestError(
                409,
                f"Error occurred while deleting the ThousandEyes alert stream input. Error: {e}. Please check the logs.",
            )

    def _setup_webhook_integration(self, old_alert_rules=None):
        """Setup webhook integration for alerts
        
        :param old_alert_rules: Previous alert rules (for detecting removed rules during edit)
        """
        config = self._get_webhook_config()
        if not config:
            return

        thousandeyes_client = ThousandEyesClient(
            self.getSessionKey(), config["thousandeyes_user"], logger
        )
        aid = config["aid"]

        # Track newly created resources for potential rollback
        created_operation_id = None
        created_connector_id = None

        try:
            # Step 1: Create or update webhook operation
            operation_id, created_operation_id = self._setup_webhook_operation(
                thousandeyes_client, aid, config["existing_operation_id"]
            )

            # Step 2: Create or update webhook connector
            connector_id, created_connector_id = self._setup_webhook_connector(
                thousandeyes_client, aid, config, created_operation_id
            )

            # Step 3: Finalize setup - assign to connector and configure alert rules
            self._finalize_webhook_setup(
                thousandeyes_client, aid, config,
                operation_id, connector_id,
                old_alert_rules, created_operation_id, created_connector_id
            )

            # Store webhook IDs for cleanup
            self.payload["webhook_operation_id"] = operation_id
            self.payload["webhook_connector_id"] = connector_id

        except Exception as e:
            logger.error(f"Error setting up webhook integration: {str(e)}")
            raise

    def _get_webhook_config(self):
        """Extract and validate webhook configuration from payload
        
        :return: Configuration dict or None if required fields are missing
        """
        alert_rules = self.payload.get("alert_rules", "")
        hec_target = self.payload.get("hec_target")
        hec_token = self.payload.get("hec_token")

        if not alert_rules or not hec_target or not hec_token:
            return None

        thousandeyes_acc_group = self.payload.get("thousandeyes_acc_group")

        return {
            "thousandeyes_user": self.payload.get("thousandeyes_user"),
            "aid": get_account_id(thousandeyes_acc_group),
            "alert_rules": alert_rules,
            "hec_target": hec_target,
            "hec_token": hec_token,
            "existing_operation_id": self.payload.get("webhook_operation_id"),
            "existing_connector_id": self.payload.get("webhook_connector_id"),
        }

    def _setup_webhook_operation(self, client, aid, existing_operation_id):
        """Create or update webhook operation
        
        :param client: ThousandEyesClient instance
        :param aid: Account group ID
        :param existing_operation_id: Existing operation ID (None for new)
        :return: Tuple of (operation_id, created_operation_id or None)
        """
        operation_id = self._create_or_update_webhook_operation(
            client, aid, existing_operation_id
        )

        created_operation_id = None
        if existing_operation_id is None:
            created_operation_id = operation_id
            logger.debug(f"Created new webhook operation: {operation_id}")

        return operation_id, created_operation_id

    def _setup_webhook_connector(self, client, aid, config, created_operation_id):
        """Create or update webhook connector with rollback on failure
        
        :param client: ThousandEyesClient instance
        :param aid: Account group ID
        :param config: Webhook configuration dict
        :param created_operation_id: Newly created operation ID (for rollback)
        :return: Tuple of (connector_id, created_connector_id or None)
        """
        try:
            connector_id = self._create_or_update_webhook_connector(
                client,
                config["hec_target"],
                config["hec_token"],
                aid,
                config["existing_connector_id"],
            )

            created_connector_id = None
            if config["existing_connector_id"] is None:
                created_connector_id = connector_id
                logger.debug(f"Created new webhook connector: {connector_id}")

            return connector_id, created_connector_id

        except Exception:
            if created_operation_id:
                logger.warning(f"Connector creation failed, rolling back operation {created_operation_id}")
                self._rollback_webhook_resources(client, aid, operation_id=created_operation_id)
            raise

    def _finalize_webhook_setup(
        self, client, aid, config, operation_id, connector_id,
        old_alert_rules, created_operation_id, created_connector_id
    ):
        """Finalize webhook setup: assign to connector and configure alert rules
        
        :param client: ThousandEyesClient instance
        :param aid: Account group ID
        :param config: Webhook configuration dict
        :param operation_id: Webhook operation ID
        :param connector_id: Webhook connector ID
        :param old_alert_rules: Previous alert rules (for edit operations)
        :param created_operation_id: Newly created operation ID (for rollback)
        :param created_connector_id: Newly created connector ID (for rollback)
        """
        try:
            self._assign_operation_to_connector(client, operation_id, connector_id, aid)

            # Handle removed alert rules (during edit)
            if old_alert_rules:
                self._handle_removed_alert_rules(
                    client, aid, config["alert_rules"],
                    old_alert_rules, operation_id
                )

            # Update alert rules with webhook integration
            self._update_alert_rules(client, config["alert_rules"], operation_id, aid)

        except Exception:
            if created_connector_id or created_operation_id:
                logger.warning("Alert rule update failed, rolling back webhook resources")
                self._rollback_webhook_resources(
                    client, aid,
                    operation_id=created_operation_id,
                    connector_id=created_connector_id
                )
            raise

    def _handle_removed_alert_rules(self, client, aid, new_alert_rules, old_alert_rules, operation_id):
        """Handle alert rules that were removed during an edit operation
        
        :param client: ThousandEyesClient instance
        :param aid: Account group ID
        :param new_alert_rules: New alert rules string (tilde-separated)
        :param old_alert_rules: Previous alert rules string (tilde-separated)
        :param operation_id: Webhook operation ID
        """
        old_rules_set = {r.strip() for r in old_alert_rules.split("~") if r.strip()}
        new_rules_set = {r.strip() for r in new_alert_rules.split("~") if r.strip()}
        removed_rules = old_rules_set - new_rules_set

        if removed_rules:
            logger.info(f"Removing webhook integration from {len(removed_rules)} alert rules")
            removed_rules_str = "~".join(removed_rules)
            self._remove_webhook_from_alert_rules(client, removed_rules_str, operation_id, aid)

    def _create_or_update_webhook_operation(self, client, aid, operation_id=None):
        """Create or update webhook operation"""
        alerts_index = self.payload.get("alerts_index", "main")

        # Add index to template (can't use JSON parsing due to Handlebars syntax)
        webhook_payload_with_index = THOUSANDEYES_WEBHOOK_PAYLOAD_TEMPLATE.replace(
            '"index": "alerts_index",', f'"index": "{alerts_index}",'
        )

        payload = {
            "name": f"Splunk Alert Webhook - {self.callerArgs.id}",
            "status": "connected",
            "category": "alerts",
            "type": "webhook",
            "payload": webhook_payload_with_index,
            "headers": [],
            "enabled": True
        }

        logger.debug(
            f"Webhook operation payload - name: {payload['name']}, "
            f"id: {operation_id if operation_id else 'None (creating new)'}"
        )

        try:
            if operation_id:
                # update existing operation
                payload["id"] = operation_id
                client.update_webhook_operation(operation_id, payload, aid)
                return operation_id
            else:
                # create new operation
                response = client.create_webhook_operation(payload, aid)
                logger.debug(f"Webhook operation created with ID: {response['id']}")
                return response["id"]
        except Exception as e:
            error_msg = str(e)
            if "409" in error_msg:
                error_message = f"An alert stream input with the name '{self.callerArgs.id}' already exists."
                logger.error(error_message)
                raise Exception(error_message)
            raise

    def _create_or_update_webhook_connector(
        self,
        client,
        hec_target,
        hec_token,
        aid,
        connector_id=None,
    ):
        """Create or update webhook connector"""
        session_key = self.getSessionKey()
        actual_hec_token = get_single_hec_token(session_key, hec_token).get("token")
        payload = {
            "type": "generic",
            "name": f"Splunk Connector - {self.callerArgs.id}",
            "target": hec_target,
            "headers": [
                {"name": "Content-Type", "value": "application/json"},
                {"name": "Authorization", "value": f"Splunk {actual_hec_token}"},
            ],
        }

        logger.debug(
            f"Webhook connector payload - name: {payload['name']}, "
            f"id: {connector_id if connector_id else 'None (creating new)'}"
        )

        try:
            if connector_id:
                # update existing connector
                payload["id"] = connector_id
                client.update_connector(connector_id, payload, aid)
                return connector_id
            else:
                # create new connector
                response = client.create_connector(payload, aid)
                logger.debug(f"Webhook connector created with ID: {response['id']}")
                return response["id"]
        except Exception as e:
            error_msg = str(e)
            if "409" in error_msg:
                error_message = f"An alert stream input with the name '{self.callerArgs.id}' already exists. "
                logger.error(error_message)
                raise Exception(error_message)
            if "URL cannot be targeted" in error_msg:
                error_message = "The HEC Target is invalid. URL cannot be targeted."
                logger.error(error_message)
                raise Exception(error_message)
            raise

    def _assign_operation_to_connector(self, client, operation_id, connector_id, aid):
        """Assign connector to operation"""
        operation_ids = [operation_id]
        client.assign_operations_to_connector(operation_ids, connector_id, aid)

    def _update_alert_rules(self, client, alert_rules, operation_id, aid):
        """Update alert rules with webhook integration"""
        alert_rules_list = alert_rules.split("~")

        for rule_id in alert_rules_list:
            if rule_id.strip():
                try:
                    # Get current rule
                    rule_data = client.get_alert_rule(rule_id, aid)

                    logger.debug(f"Fetched alert rule {rule_id} for webhook integration")

                    # Add customWebhook integration
                    if "notifications" not in rule_data:
                        rule_data["notifications"] = {}

                    if "customWebhook" not in rule_data["notifications"]:
                        rule_data["notifications"]["customWebhook"] = []

                    # Add our webhook integration
                    webhook_integration = {"integrationId": operation_id}

                    # Check if already exists
                    existing = any(
                        w.get("integrationId") == operation_id
                        for w in rule_data["notifications"]["customWebhook"]
                    )
                    if not existing:
                        rule_data["notifications"]["customWebhook"].append(
                            webhook_integration
                        )

                    # Update the rule
                    client.update_alert_rule(rule_id, rule_data, aid)
                    logger.info(f"Successfully updated alert rule {rule_id} with webhook integration")
                    
                except Exception as e:
                    logger.error(
                        f"Failed to update alert rule {rule_id} with webhook integration: {str(e)}"
                    )
                    # Continue with other rules even if one fails
                    continue

    def _remove_webhook_from_alert_rules(self, client, alert_rules, operation_id, aid):
        """Remove webhook integration from alert rules"""
        alert_rules_list = alert_rules.split("~")

        for rule_id in alert_rules_list:
            if rule_id.strip():
                try:
                    # Get current rule
                    rule_data = client.get_alert_rule(rule_id, aid)

                    logger.debug(f"Fetched alert rule {rule_id} to remove webhook integration")

                    # Remove webhook integration if it exists
                    webhooks = rule_data.get("notifications", {}).get("customWebhook")
                    if webhooks:
                        original_count = len(rule_data["notifications"]["customWebhook"])
                        rule_data["notifications"]["customWebhook"] = [
                            w for w in rule_data["notifications"]["customWebhook"]
                            if w.get("integrationId") != operation_id
                        ]
                        new_count = len(rule_data["notifications"]["customWebhook"])
                        
                        if original_count > new_count:
                            # ThousandEyes API quirk: to clear customWebhook, we must send [{}] not []
                            if new_count == 0:
                                rule_data["notifications"]["customWebhook"] = [{}]
                            
                            # Update the rule only if we removed something
                            client.update_alert_rule(rule_id, rule_data, aid)
                            logger.info(f"Successfully removed webhook integration from alert rule {rule_id}")
                        else:
                            logger.debug(f"Webhook integration not found in alert rule {rule_id}, skipping")
                    else:
                        logger.debug(f"No customWebhook notifications in alert rule {rule_id}, skipping")
                    
                except Exception as e:
                    logger.warning(
                        f"Failed to remove webhook integration from alert rule {rule_id}: {str(e)}"
                    )
                    # Continue with other rules even if one fails
                    continue

    def _rollback_webhook_resources(self, client, aid, operation_id=None, connector_id=None):
        """Rollback newly created webhook resources on failure
        
        :param client: ThousandEyesClient instance
        :param aid: Account group ID
        :param operation_id: Webhook operation ID to delete (if created)
        :param connector_id: Webhook connector ID to delete (if created)
        """
        # Delete in reverse order of creation: connector first, then operation
        if connector_id:
            try:
                logger.info(f"Rollback: Deleting webhook connector {connector_id}")
                client.delete_connector(connector_id, aid)
            except Exception as e:
                logger.error(f"Failed to rollback connector {connector_id}: {str(e)}")
        
        if operation_id:
            try:
                logger.info(f"Rollback: Deleting webhook operation {operation_id}")
                client.delete_webhook_operation(operation_id, aid)
            except Exception as e:
                logger.error(f"Failed to rollback operation {operation_id}: {str(e)}")

    def _cleanup_webhook_integration(self):
        """Clean up webhook integration when deleting input"""
        session_key = self.getSessionKey()

        # Get current configuration
        _, response_content = rest.simpleRequest(
            f"/servicesNS/nobody/{THOUSANDEYES_TA_NAME}/data/inputs/alerts_stream/{self.callerArgs.id}",
            sessionKey=session_key,
            method="GET",
            getargs={"output_mode": "json"},
            raiseAllErrors=True,
        )
        response_content = json.loads(response_content)["entry"][0]["content"]

        thousandeyes_user = response_content.get("thousandeyes_user")
        thousandeyes_acc_group = response_content.get("thousandeyes_acc_group")
        alert_rules = response_content.get("alert_rules", "")
        webhook_operation_id = response_content.get("webhook_operation_id")
        webhook_connector_id = response_content.get("webhook_connector_id")

        if not thousandeyes_user:
            logger.warning("No ThousandEyes user found for cleanup")
            return

        if not webhook_operation_id and not webhook_connector_id:
            logger.info("No webhook resources found, skipping cleanup")
            return

        aid = get_account_id(thousandeyes_acc_group)
        thousandeyes_client = ThousandEyesClient(
            session_key, thousandeyes_user, logger
        )

        cleanup_failed = False

        # First, remove webhook integration from alert rules
        if alert_rules and webhook_operation_id:
            try:
                self._remove_webhook_from_alert_rules(
                    thousandeyes_client,
                    alert_rules,
                    webhook_operation_id,
                    aid
                )
            except Exception as e:
                logger.error(f"Failed to remove webhook from alert rules: {str(e)}")
                cleanup_failed = True

        # Then delete webhook operation
        if webhook_operation_id:
            try:
                thousandeyes_client.delete_webhook_operation(
                    webhook_operation_id, aid
                )
                logger.info(f"Successfully deleted webhook operation {webhook_operation_id}")
            except Exception as e:
                logger.error(f"Failed to delete webhook operation {webhook_operation_id}: {str(e)}")
                cleanup_failed = True

        # Finally delete webhook connector
        if webhook_connector_id:
            try:
                thousandeyes_client.delete_connector(webhook_connector_id, aid)
                logger.info(f"Successfully deleted webhook connector {webhook_connector_id}")
            except Exception as e:
                logger.error(f"Failed to delete webhook connector {webhook_connector_id}: {str(e)}")
                cleanup_failed = True

        # If any cleanup operations failed, raise a generic error
        if cleanup_failed:
            error_message = "Failed to clean up ThousandEyes resources. Please check the logs for details."
            logger.error(error_message)
            raise Exception(error_message)
