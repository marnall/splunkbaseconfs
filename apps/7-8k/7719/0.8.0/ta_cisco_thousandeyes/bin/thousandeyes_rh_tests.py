import os
import traceback

import import_declare_test  # noqa: F401
import splunk.admin as admin
from log_helper import setup_logging
from thousandeyes_client import ThousandEyesClient
from thousandeyes_constant import DEFAULT_ENDPOINT_DYNAMIC_TEST_TYPE
from thousandeyes_utils import get_account_id

logger = setup_logging(os.path.splitext(os.path.basename(__file__))[0].lower())


class ThousandeyesTests(admin.MConfigHandler):
    """
    Get the Tests data.

    :param admin.MConfigHandler: inhereting admin.MConfigHandler
    """

    def setup(self):
        """To setup the variables to access in handlelist."""
        self.supportedArgs.addReqArg("thousandeyes_user")
        self.supportedArgs.addReqArg("thousandeyes_acc_group")
        self.supportedArgs.addOptArg("signal")
        self.supportedArgs.addOptArg("test_domain")

    def handleList(self, conf_info):
        """
        Handle the list operation to populate the Tests.

        :param conf_info: Default parameter generated by UCC which is used to pass the response
        """
        session_key = self.getSessionKey()
        thousandeyes_user = self.callerArgs.data.get("thousandeyes_user")[0]
        account_group = self.callerArgs.data.get("thousandeyes_acc_group")[0]

        signal = self.callerArgs.data.get("signal", [None])[0]
        test_domain = self.callerArgs.data.get("test_domain", [None])[0]

        aid = get_account_id(account_group)

        try:
            logger.info(
                f"Fetching {test_domain} tests with signal {signal} from user {thousandeyes_user} account id {aid}."
            )
            client = ThousandEyesClient(session_key, thousandeyes_user, logger)
            if test_domain and test_domain.lower() == "cea":
                if signal and signal.lower() == "trace":
                    tests = client.get_cea_tests_for_traces(aid)
                else:
                    tests = client.get_all_cea_tests_for_metrics(aid)
            else:
                tests = client.get_all_endpoint_tests(aid)
            if len(tests.get("tests")) > 0:
                # Space added before the value All to order All always at the top.
                conf_info[" All"]
                conf_info[" All"].append("display_name", " All")
                conf_info[" All"].append("name", " All")
            else:
                logger.info(
                    f"No tests configured for user {thousandeyes_user} account id {aid}."
                )
            for test in tests["tests"]:
                enabled = "Enabled" if test.get("enabled") or test.get("isEnabled") else "Disabled"
                
                # For endpoint tests, include the subtype in the name
                endpoint_test_category = test.get("_endpointTestCategory")
                test_type = test.get("type", DEFAULT_ENDPOINT_DYNAMIC_TEST_TYPE)

                if endpoint_test_category:
                    display_name = f"{test['testName']} ({test['testId']} | {test_type} | {endpoint_test_category} | {enabled})"
                    name = f"{test['testName']} ({test['testId']} | {test_type} | {endpoint_test_category})"
                else:
                    display_name = f"{test['testName']} ({test['testId']} | {test_type} | {enabled})"
                    name = f"{test['testName']} ({test['testId']} | {test_type})"
                
                conf_info[test.get("testId")].append("display_name", display_name)
                conf_info[test.get("testId")].append("name", name)

            logger.info(
                f"Successfully fetched {test_domain} tests with signal {signal} from user {thousandeyes_user} account id {aid}."
            )
        except Exception as e:
            logger.error(
                f"Error occurred while fetching {test_domain} tests with signal {signal}: {e}"
                f" {traceback.format_exc()}"
            )
            raise Exception(
                f"Error occurred while fetching {test_domain} tests with signal {signal}: {e} Please check the logs."
            )


if __name__ == "__main__":
    """Driving function."""
    admin.init(ThousandeyesTests, admin.CONTEXT_NONE)
