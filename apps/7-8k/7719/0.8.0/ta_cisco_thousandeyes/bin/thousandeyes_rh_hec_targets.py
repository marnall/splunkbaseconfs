import import_declare_test  # noqa: F401
import splunk.admin as admin

from thousandeyes_hec_helpers import generate_stream_url_list

class HECTargets(admin.MConfigHandler):
    """
    Get the HEC Targets to prefill stream create input.

    :param admin.MConfigHandler: inhereting admin.MConfigHandler
    """

    def setup(self):
        """To setup the variables to access in handlelist."""
        pass

    def handleList(self, conf_info):
        """
        Handle the list operation to populate the HEC Targets.

        :param conf_info: Default parameter generated by UCC which is used to pass the response
        """
        session_key = self.getSessionKey()

        for url in generate_stream_url_list(session_key):
            conf_info[url] = (url, url)


if __name__ == "__main__":
    """Driving function."""
    admin.init(HECTargets, admin.CONTEXT_NONE)
