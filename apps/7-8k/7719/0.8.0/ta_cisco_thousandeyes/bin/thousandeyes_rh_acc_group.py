import os
import traceback
import import_declare_test  # noqa: F401
import splunk.admin as admin

from log_helper import setup_logging
from thousandeyes_client import ThousandEyesClient

logger = setup_logging(os.path.splitext(os.path.basename(__file__))[0].lower())


class AccountGroup(admin.MConfigHandler):
    """
    Get the Account Groups data.

    :param admin.MConfigHandler: inhereting admin.MConfigHandler
    """

    def setup(self):
        """To setup the variables to access in handlelist."""
        self.supportedArgs.addReqArg("thousandeyes_user")
        self.supportedArgs.addOptArg("all_account_group")

    def handleList(self, conf_info):
        """
        Handle the list operation to populate the Account Groups.

        :param conf_info: Default parameter generated by UCC which is used to pass the response
        """
        session_key = self.getSessionKey()
        thousandeyes_user = self.callerArgs.data.get("thousandeyes_user")[0]

        try:
            logger.info(f"Fetching Account Groups from user {thousandeyes_user}.")
            client = ThousandEyesClient(session_key, thousandeyes_user, logger)
            acc_groups = client.get_all_acc_groups()

            all_option = self.callerArgs.data.get("all_account_group")
            if all_option and all_option[0] == "1":
                # Space added before the value All to order All always at the top.
                conf_info[" All"]

            for ag in acc_groups["accountGroups"]:
                name = f"{ag['accountGroupName']} ({ag['aid']})"
                conf_info[name].append("id", ag["aid"])

            logger.info(
                f"Successfully fetched Account Groups from user {thousandeyes_user}."
            )

        except Exception as e:

            logger.error(
                f"Error occurred while fetching Account Groups: {e}"
                f" {traceback.format_exc()}"
            )
            raise Exception(
                f"Error while fetching Account Groups: {e} Please check the logs."
            )


if __name__ == "__main__":
    """Driving function."""
    admin.init(AccountGroup, admin.CONTEXT_NONE)
