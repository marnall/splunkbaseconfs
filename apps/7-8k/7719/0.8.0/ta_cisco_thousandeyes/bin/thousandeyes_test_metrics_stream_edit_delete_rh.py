import os
import json
import traceback
import splunk.rest as rest
import import_declare_test  # noqa: F401
from thousandeyes_constant import THOUSANDEYES_TA_NAME # noqa E402
from splunktaucclib.rest_handler.error import RestError
from splunktaucclib.rest_handler.admin_external import AdminExternalHandler
from log_helper import setup_logging
from thousandeyes_client import ThousandEyesClient
from thousandeyes_validator_test_metrics_stream import StreamInputValidator
from thousandeyes_utils import get_account_id

logger = setup_logging(os.path.splitext(os.path.basename(__file__))[0].lower())


class StreamInputHandler(AdminExternalHandler):
    """
    Manage the Rest Handler for Test Stream Input.

    :param AdminExternalHandler: inhereting AdminExternalHandler
    """

    def __init__(self, *args, **kwargs):
        """
        Initialize Rest Handler.

        :param args: positional arguments.
        :param kwargs: key value arguments.

        :return: StreamInputHandler Object
        """
        AdminExternalHandler.__init__(self, *args, **kwargs)

    def handleCreate(self, conf_info):
        """
        Handle the create operation.

        :param conf_info: Default parameter generated by UCC which is used to pass the response.
        """
        try:
            logger.info(
                f"Creating the ThousandEyes Stream input: {self.callerArgs.id}."
            )
            stream_validator = StreamInputValidator(self.getSessionKey(), logger)
            stream_validator.validate(self.payload)
            super().handleCreate(conf_info)
            logger.info(
                f"Sucessfully created the ThousandEyes Stream input: {self.callerArgs.id}."
            )
        except Exception as e:
            logger.error(
                f"Error occurred while creating ThousandEyes stream input: {self.callerArgs.id}."
                f"Error: {e} {traceback.format_exc()}"
            )
            raise RestError(
                409,
                f"Error while configuring ThousandEyes stream input."
                f"Error: {e} Please check the logs.",
            )

    def handleEdit(self, conf_info):
        """
        Handle the edit operation.

        :param conf_info: Default parameter generated by UCC which is used to pass the response
        """
        try:
            if self.payload.get("disabled"):
                enabled = False if self.payload.get("disabled") == "true" else True
                if enabled:
                    logger.info(
                        f"Enabling the ThousandEyes Stream input: {self.callerArgs.id}."
                    )
                else:
                    logger.info(
                        f"Disabling the ThousandEyes Stream input: {self.callerArgs.id}."
                    )
                session_key = self.getSessionKey()
                logger.info(
                    f"Fetching the ThousandEyes Account for input: {self.callerArgs.id}"
                )
                _, response_content = rest.simpleRequest(
                    f"/servicesNS/nobody/{THOUSANDEYES_TA_NAME}/data/inputs/test_metrics_stream/{self.callerArgs.id}",
                    sessionKey=session_key,
                    method="GET",
                    getargs={"output_mode": "json"},
                    raiseAllErrors=True,
                )
                response_content = json.loads(response_content)["entry"][0]["content"]
                thousandeyes_user = response_content.get("thousandeyes_user")
                thousandeyes_acc_group = response_content.get("thousandeyes_acc_group")
                thousandeyes_acc_group_id = get_account_id(thousandeyes_acc_group)
                thousandeyes_stream_id = response_content.get("thousandeyes_stream_id")
                logger.info(
                    f"ThousandEyes account for input: {self.callerArgs.id} is : {thousandeyes_user}"
                )
                thousandeyes_client = ThousandEyesClient(
                    session_key, thousandeyes_user, logger
                )
                _ = thousandeyes_client.update_stream(  # noqa F841
                    thousandeyes_acc_group_id,
                    thousandeyes_stream_id,
                    json.dumps({"enabled": enabled}),
                )
                if enabled:
                    logger.info(
                        f"Successfully enabled the ThousandEyes stream input: {self.callerArgs.id}."
                    )
                else:
                    logger.info(
                        f"Successfully disabled the ThousandEyes stream input: {self.callerArgs.id}."
                    )
            else:
                logger.info(
                    f"Updating the ThousandEyes Stream input: {self.callerArgs.id}."
                )
                stream_validator = StreamInputValidator(self.getSessionKey(), logger)
                stream_validator.validate(self.payload)
                logger.info(
                    f"Sucessfully updated the ThousandEyes Stream input: {self.callerArgs.id}."
                )
        except Exception as e:
            logger.error(
                f"Error occurred while editing the ThousandEyes stream input: {self.callerArgs.id}."
                f"Error: {e} {traceback.format_exc()}"
            )
            raise RestError(
                409,
                f"Error occurred while editing the ThousandEyes stream input."
                f"Error: {e} Please check the logs.",
            )

        super().handleEdit(conf_info)

    def handleRemove(self, conf_info):
        """
        Handle the delete operation.

        :param conf_info: Default parameter generated by UCC which is used to pass the response
        """
        try:
            logger.info(
                f"Deleting the ThousandEyes Stream input: {self.callerArgs.id}."
            )
            session_key = self.getSessionKey()
            logger.info(
                f"Fetching the ThousandEyes Account for input: {self.callerArgs.id}"
            )
            _, response_content = rest.simpleRequest(
                f"/servicesNS/nobody/{THOUSANDEYES_TA_NAME}/data/inputs/test_metrics_stream/{self.callerArgs.id}",
                sessionKey=session_key,
                method="GET",
                getargs={"output_mode": "json"},
                raiseAllErrors=True,
            )
            response_content = json.loads(response_content)["entry"][0]["content"]
            thousandeyes_user = response_content.get("thousandeyes_user")
            thousandeyes_acc_group = response_content.get("thousandeyes_acc_group")
            thousandeyes_acc_group_id = get_account_id(thousandeyes_acc_group)
            thousandeyes_stream_id = response_content.get("thousandeyes_stream_id")
            logger.info(
                f"ThousandEyes account for input: {self.callerArgs.id} is : {thousandeyes_user}"
            )
            thousandeyes_client = ThousandEyesClient(
                session_key, thousandeyes_user, logger
            )
            _ = thousandeyes_client.delete_stream(
                thousandeyes_acc_group_id, thousandeyes_stream_id
            )  # noqa F841
            logger.info(
                f"Successfully deleted the ThousandEyes stream input: {self.callerArgs.id}."
            )
            _, response_content = rest.simpleRequest(
                f"/servicesNS/nobody/{THOUSANDEYES_TA_NAME}/storage/collections/data/{THOUSANDEYES_TA_NAME}_checkpointer/{self.callerArgs.id}",
                sessionKey=session_key,
                method="DELETE",
                getargs={"output_mode": "json"},
                raiseAllErrors=True,
            )
            super().handleRemove(conf_info)
            logger.info(
                f"Successfully deleted checkpoint for ThousandEyes stream input: {self.callerArgs.id}."
            )
        except Exception as e:
            logger.error(
                f"Error occurred while deleting the ThousandEyes stream input: {self.callerArgs.id}."
                f"Error: {e} {traceback.format_exc()}"
            )
            raise RestError(
                409,
                f"Error occurred while deleting the ThousandEyes stream input."
                f"Error: {e} Please check the logs.",
            )
