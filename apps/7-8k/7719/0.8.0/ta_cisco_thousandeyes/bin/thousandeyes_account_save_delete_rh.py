import os
import traceback

from solnlib import conf_manager
from splunktaucclib.rest_handler.admin_external import AdminExternalHandler
from splunktaucclib.rest_handler.error import RestError

import import_declare_test  # noqa 401
from log_helper import setup_logging
from thousandeyes_account_manager import ThousandEyesAccountManager
from thousandeyes_constant import THOUSANDEYES_TA_NAME  # noqa E402

logger = setup_logging(os.path.splitext(os.path.basename(__file__))[0].lower())


class CustomAccountHandler(AdminExternalHandler):
    """
    Manage the Rest Handler for Account.

    :param AdminExternalHandler: inhereting AdminExternalHandler
    """

    def __init__(self, *args, **kwargs):
        """
        Initialize Rest Handler.

        :param args: positional arguments.
        :param kwargs: key value arguments.

        :return: CustomAccountHandler Object
        """
        AdminExternalHandler.__init__(self, *args, **kwargs)

    def handleRemove(self, conf_info):
        """
        Handle the delete thousandeyes user operation.

        :param conf_info: Default parameter generated by UCC which is used to pass the response.
        """
        session_key = self.getSessionKey()
        try:
            logger.info(f"Deleting the user {self.callerArgs.id}.")
            logger.info("Fetching configured input details.")
            conf_file = conf_manager.ConfManager(
                session_key,
                THOUSANDEYES_TA_NAME,
                realm="__REST_CREDENTIAL__#{}#configs/conf-{}".format(
                    THOUSANDEYES_TA_NAME, "inputs"
                ),
            ).get_conf("inputs")

            inputs_file = conf_file.get_all(only_current_app=True)
            logger.info("Successfully fetched configured input details.")
            created_inputs = list(inputs_file.keys())
            input_list = []

            for each in created_inputs:
                configured_input = each.split("://")
                configured_account = inputs_file.get(each).get("thousandeyes_user")
                if configured_account == self.callerArgs.id:
                    input_list.append(configured_input[1])
            if len(input_list) > 0:
                raise Exception(
                    "Account can not be deleted because it is linked with the "
                    f"following inputs: {(', '.join(input_list))}"
                )
            else:
                super().handleRemove(conf_info)
                logger.info(f"Successfully deleted the user {self.callerArgs.id}.")
        except Exception as e:
            logger.error(
                f"Error occurred while deleting user {self.callerArgs.id}."
                f"Error: {e} {traceback.format_exc()}"
            )
            raise RestError(409, e)

    def handleCreate(self, conf_info):
        """
        Handle the create thousandeyes user operation.

        :param conf_info: Default parameter generated by UCC which is used to pass the response.
        """
        try:
            logger.info(f"Saving user {self.callerArgs.id}.")
            del self.payload["device_code"]
            del self.payload["user_code"]
            del self.payload["verification_url"]
            del self.payload["code"]
            super().handleCreate(conf_info)
            logger.info(f"Successfully saved user {self.callerArgs.id}.")
        except Exception as e:
            logger.error(
                f"Error occurred while saving user {self.callerArgs.id}."
                f"Error: {e} {traceback.format_exc()}"
            )
            raise RestError(409, e)

    def handleEdit(self, conf_info):
        """
        Handle the edit (re-authorization) of ThousandEyes user.
        This method attempts to create a new stanza for the user. If the stanza already exists,
        it updates the access token using the `ThousandEyesAccountManager`.

        :param conf_info: Default parameter generated by UCC which is used to pass the response.
        """
        try:
            logger.info(f"handleEdit: upsert user {self.callerArgs.id}")

            # Clean up transient fields
            for key in ("device_code", "user_code", "verification_url", "code"):
                self.payload.pop(key, None)

            try:
                super().handleCreate(conf_info)
                logger.info(f"Created new stanza for '{self.callerArgs.id}'")
            except RestError as e:
                msg = str(e).lower()
                if "already in use" in msg:
                    logger.info("Stanza exists → performing update via ThousandEyesAccountManager")
                    session_key = self.getSessionKey()
                    te_account_manager = ThousandEyesAccountManager(session_key)
                    te_account_manager.update_access_token(
                        self.payload["access_token"],
                        self.payload["refresh_token"],
                        self.callerArgs.id,
                    )
                    logger.info(f"Updated stanza for '{self.callerArgs.id}'")
                else:
                    raise

        except Exception as e:
            logger.error(
                f"Error in handleEdit for '{self.callerArgs.id}': {e}\n{traceback.format_exc()}"
            )
            raise RestError(409, f"Edit failed: {e}")

