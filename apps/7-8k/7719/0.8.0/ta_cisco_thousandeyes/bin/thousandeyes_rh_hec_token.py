import os
import traceback
import import_declare_test  # noqa: F401
import splunk.admin as admin
from thousandeyes_utils import get_hec_tokens

from log_helper import setup_logging

logger = setup_logging(os.path.splitext(os.path.basename(__file__))[0].lower())


class HEC(admin.MConfigHandler):
    """
    Get the HEC data.

    :param admin.MConfigHandler: inhereting admin.MConfigHandler
    """

    def setup(self):
        """To setup the variables to access in handlelist."""
        pass

    def handleList(self, conf_info):
        """
        Handle the list operation to populate the HECs.

        :param conf_info: Default parameter generated by UCC which is used to pass the response
        """
        session_key = self.getSessionKey()

        try:
            logger.info("Fetching configured HECs from splunk instance.")

            hec_list = get_hec_tokens(session_key)
            for hec_input in hec_list.keys():
                name = hec_input
                conf_info[name] = (hec_input, hec_list[hec_input])

            logger.info("Successfully fetched all HECs.")

        except Exception as e:
            logger.error(
                f"Error occurred while fetching HECs: {e} {traceback.format_exc()}"
            )
            raise Exception(f"Error while fetching HECs: {e} Please check the logs.")


if __name__ == "__main__":
    """Driving function."""
    admin.init(HEC, admin.CONTEXT_NONE)
