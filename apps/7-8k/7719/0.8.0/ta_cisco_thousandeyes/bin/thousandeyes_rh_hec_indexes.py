import os
import traceback
import import_declare_test  # noqa: F401
import splunk.admin as admin
from thousandeyes_utils import get_single_hec_token, get_event_indexes

from log_helper import setup_logging

logger = setup_logging(os.path.splitext(os.path.basename(__file__))[0].lower())


class HECIndex(admin.MConfigHandler):
    """
    Get the HEC Indexes.

    :param admin.MConfigHandler: inhereting admin.MConfigHandler
    """

    def setup(self):
        """To setup the variables to access in handlelist."""
        self.supportedArgs.addReqArg("hec_token")

    def handleList(self, conf_info):
        """
        Handle the list operation to populate the HEC Indexes.

        :param conf_info: Default parameter generated by UCC which is used to pass the response
        """
        session_key = self.getSessionKey()
        hec_token = self.callerArgs.data.get("hec_token")[0]

        try:
            logger.info(
                f"Fetching event indexes configured for HEC {hec_token} from splunk."
            )

            data = get_single_hec_token(session_key, hec_token)
            if data is None:
                raise Exception(
                    "Unable to fetch Indexes from HEC. Please check the logs."
                )

            hec_indexes = data.get("indexes", [])
            hec_indexes.append(data.get("index"))
            hec_indexes = list(set(hec_indexes))
            if len(hec_indexes) == 0:
                raise Exception(f"No indexes configured for HEC {hec_token}.")

            logger.info("Fetching event Indexes from splunk.")
            event_indexes = get_event_indexes(session_key)
            if len(event_indexes) == 0:
                raise Exception("No event indexes found in splunk instance.")

            hec_event_indexes = [
                index
                for index in hec_indexes
                if index in event_indexes and not index.startswith("_")
            ]
            if "default" in hec_indexes:
                hec_event_indexes.append("default")

            if len(hec_event_indexes) == 0:
                raise Exception(f"No event indexes configured for HEC {hec_token}.")

            for index in hec_event_indexes:
                conf_info[index] = (index, index)

            logger.info(
                f"Successfully fetched event Indexes configured for HEC {hec_token}."
            )

        except Exception as e:
            logger.error(
                f"Error occurred while fetching event Indexes configured for HEC:{hec_token}. Error:{e}"
                f" {traceback.format_exc()}"
            )
            raise Exception(
                f"Error while fetching event indexes configured for HEC {hec_token}."
                f"Error:{e} Please check the logs."
            )


if __name__ == "__main__":
    """Driving function."""
    admin.init(HECIndex, admin.CONTEXT_NONE)
