import os
import traceback

import splunk.admin as admin

import import_declare_test  # noqa: F401
from log_helper import setup_logging
from thousandeyes_client import ThousandEyesClient
from thousandeyes_utils import get_account_id

logger = setup_logging(os.path.splitext(os.path.basename(__file__))[0].lower())


class ThousandEyesTags(admin.MConfigHandler):
    """
    Get the Tags data.

    :param admin.MConfigHandler: inheriting admin.MConfigHandler
    """

    def setup(self):
        """To set up the variables to access in handlelist."""
        self.supportedArgs.addReqArg("thousandeyes_user")
        self.supportedArgs.addReqArg("thousandeyes_acc_group")

    def handleList(self, conf_info):
        """
        Handle the list operation to populate the Tags.

        :param conf_info: Default parameter generated by UCC which is used to pass the response
        """
        session_key = self.getSessionKey()
        thousandeyes_user = self.callerArgs.data.get("thousandeyes_user")[0]
        account_group = self.callerArgs.data.get("thousandeyes_acc_group")[0]

        aid = get_account_id(account_group)

        try:
            logger.info(
                f"Fetching tags from user {thousandeyes_user} account id {aid}."
            )
            client = ThousandEyesClient(session_key, thousandeyes_user, logger)
            tags = client.get_all_tags(aid)
            if len(tags.get("tags", [])) > 0:
                # Space added before the value All to order All always at the top.
                conf_info[" All"]
                conf_info[" All"].append("key", " All")
                conf_info[" All"].append("value", " All")

                for tag in tags.get("tags", []):
                    formatted_tag = f"{tag['key']}:{tag['value']}"
                    conf_info[formatted_tag].append("key", formatted_tag)
                    conf_info[formatted_tag].append("value", formatted_tag)

            logger.info(
                f"Successfully fetched {len(tags.get('tags', []))} tags "
                f"from user {thousandeyes_user} account id {aid}."
            )

        except Exception as e:
            logger.error(
                f"Error occurred while fetching tags: {e}. {traceback.format_exc()}"
            )
            raise Exception(
                f"Error occurred while fetching tags: {e}. Please check the logs."
            )


if __name__ == "__main__":
    """Driving function."""
    admin.init(ThousandEyesTags, admin.CONTEXT_NONE)
