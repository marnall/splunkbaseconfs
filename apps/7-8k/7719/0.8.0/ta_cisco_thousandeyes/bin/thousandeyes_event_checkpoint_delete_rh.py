import import_declare_test  # noqa 401
import splunk.rest as rest
from splunktaucclib.rest_handler.admin_external import AdminExternalHandler
import os
from log_helper import setup_logging
from thousandeyes_constant import THOUSANDEYES_TA_NAME # noqa E402
import traceback

logger = setup_logging(os.path.splitext(os.path.basename(__file__))[0].lower())


class ThousandEyesEventHandler(AdminExternalHandler):
    """
    Manage the Rest Handler for Activity Input.

    :param AdminExternalHandler: inhereting AdminExternalHandler
    """

    def __init__(self, *args, **kwargs):
        """
        Initialize Rest Handler.

        :param args: positional arguments.
        :param kwargs: key value arguments.

        :return: ThousandEyesEventHandler Object
        """
        AdminExternalHandler.__init__(self, *args, **kwargs)

    def handleRemove(self, conf_info):
        """
        Handle the delete operation.

        :param conf_info: Default parameter generated by UCC which is used to pass the response
        """
        logger.info(
            f"Deleting the checkpoint for ThousandEyes Event input: {self.callerArgs.id}."
        )
        session_key = self.getSessionKey()
        try:
            _, _ = rest.simpleRequest(
                f"/servicesNS/nobody/{THOUSANDEYES_TA_NAME}/storage/collections/data/{THOUSANDEYES_TA_NAME}_checkpointer/{self.callerArgs.id}",
                sessionKey=session_key,
                method="DELETE",
                getargs={"output_mode": "json"},
                raiseAllErrors=True,
            )
            logger.info(
                f"Successfully deleted the checkpoint for ThousandEyes Event input: {self.callerArgs.id}."
            )
        except Exception as e:
            logger.error(
                f"Error occured while deleting the checkpoint for ThousandEyes Event input: {self.callerArgs.id}."
                f"Error: {e} {traceback.format_exc()}"
            )
        finally:
            super().handleRemove(conf_info)
