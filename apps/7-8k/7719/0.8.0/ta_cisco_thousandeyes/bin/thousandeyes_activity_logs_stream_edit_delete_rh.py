import json
import os
import traceback

import import_declare_test  # noqa: F401
import splunk.rest as rest
from log_helper import setup_logging
from splunktaucclib.rest_handler.admin_external import AdminExternalHandler
from splunktaucclib.rest_handler.error import RestError
from thousandeyes_client import ThousandEyesClient
from thousandeyes_constant import THOUSANDEYES_TA_NAME  # noqa E402
from thousandeyes_utils import get_account_id
from thousandeyes_validator_activity_logs_stream import ActivityLogsStreamInputValidator

logger = setup_logging(os.path.splitext(os.path.basename(__file__))[0].lower())


class ActivityLogsStreamInputHandler(AdminExternalHandler):
    """
    Manage the Rest Handler for Activity Logs Stream Input.

    :param AdminExternalHandler: inhereting AdminExternalHandler
    """

    def __init__(self, *args, **kwargs):
        """
        Initialize Rest Handler.

        :param args: positional arguments.
        :param kwargs: key value arguments.

        :return: ActivityLogsStreamInputHandler Object
        """
        AdminExternalHandler.__init__(self, *args, **kwargs)

    def _get_stream_details(self, session_key, input_id):
        """
        Get stream details for a given input.

        :param session_key: Splunk session key
        :param input_id: Input identifier

        :return: tuple of (thousandeyes_user, thousandeyes_acc_group_id, thousandeyes_stream_id)
        """
        logger.info(
            f"Fetching the ThousandEyes Account for activity logs stream input: {input_id}"
        )
        _, response_content = rest.simpleRequest(
            f"/servicesNS/nobody/{THOUSANDEYES_TA_NAME}/data/inputs/activity_logs_stream/{input_id}",
            sessionKey=session_key,
            method="GET",
            getargs={"output_mode": "json"},
            raiseAllErrors=True,
        )
        response_content = json.loads(response_content)["entry"][0]["content"]
        thousandeyes_user = response_content.get("thousandeyes_user")
        thousandeyes_acc_group = response_content.get("thousandeyes_acc_group")
        thousandeyes_acc_group_id = get_account_id(thousandeyes_acc_group)
        thousandeyes_stream_id = response_content.get("thousandeyes_stream_id")
        logger.info(
            f"ThousandEyes account for activity logs stream input: {input_id} is : {thousandeyes_user}"
        )
        return thousandeyes_user, thousandeyes_acc_group_id, thousandeyes_stream_id

    def handleCreate(self, conf_info):
        """
        Handle the create operation.

        :param conf_info: Default parameter generated by UCC which is used to pass the response.
        """
        try:
            logger.info(
                f"Creating ThousandEyes activity logs stream input: {self.callerArgs.id}"
            )
            activity_logs_validator = ActivityLogsStreamInputValidator(
                self.getSessionKey(), logger
            )
            activity_logs_validator.validate(self.payload)
            super().handleCreate(conf_info)
            logger.info(
                f"Successfully created the ThousandEyes activity logs stream input: {self.callerArgs.id}."
            )
        except Exception as e:
            logger.error(
                f"Error occurred while creating ThousandEyes activity logs stream input: {self.callerArgs.id}."
                f"Error: {e} {traceback.format_exc()}"
            )
            raise RestError(
                409,
                f"Error while configuring ThousandEyes activity logs stream input."
                f"Error: {e} Please check the logs.",
            )

    def handleEdit(self, conf_info):
        """
        Handle the edit operation.

        :param conf_info: Default parameter generated by UCC which is used to pass the response
        """
        try:
            if self.payload.get("disabled"):
                enabled = False if self.payload.get("disabled") == "true" else True
                if enabled:
                    logger.info(
                        f"Enabling the ThousandEyes activity logs stream input: {self.callerArgs.id}."
                    )
                else:
                    logger.info(
                        f"Disabling the ThousandEyes activity logs stream input: {self.callerArgs.id}."
                    )
                session_key = self.getSessionKey()
                (
                    thousandeyes_user,
                    thousandeyes_acc_group_id,
                    thousandeyes_stream_id,
                ) = self._get_stream_details(session_key, self.callerArgs.id)
                thousandeyes_client = ThousandEyesClient(
                    session_key, thousandeyes_user, logger
                )
                _ = thousandeyes_client.update_stream(  # noqa F841
                    thousandeyes_acc_group_id,
                    thousandeyes_stream_id,
                    json.dumps({"enabled": enabled}),
                )
                if enabled:
                    logger.info(
                        f"Successfully enabled the ThousandEyes activity logs stream input: {self.callerArgs.id}."
                    )
                else:
                    logger.info(
                        f"Successfully disabled the ThousandEyes activity logs stream input: {self.callerArgs.id}."
                    )
            else:
                logger.info(
                    f"Updating the ThousandEyes activity logs stream input: {self.callerArgs.id}."
                )
                activity_logs_validator = ActivityLogsStreamInputValidator(
                    self.getSessionKey(), logger
                )
                activity_logs_validator.validate(self.payload)
                logger.info(
                    f"Successfully updated the ThousandEyes activity logs stream input: {self.callerArgs.id}."
                )
        except Exception as e:
            logger.error(
                f"Error occurred while editing the ThousandEyes activity logs stream input: {self.callerArgs.id}."
                f"Error: {e} {traceback.format_exc()}"
            )
            raise RestError(
                409,
                f"Error occurred while editing the ThousandEyes activity logs stream input."
                f"Error: {e} Please check the logs.",
            )

        super().handleEdit(conf_info)

    def handleRemove(self, conf_info):
        """
        Handle the delete operation.

        :param conf_info: Default parameter generated by UCC which is used to pass the response
        """
        logger.info(f"handleRemove {str(conf_info)}")
        try:
            logger.info(
                f"Deleting the ThousandEyes activity logs stream input: {self.callerArgs.id}."
            )
            session_key = self.getSessionKey()
            (
                thousandeyes_user,
                thousandeyes_acc_group_id,
                thousandeyes_stream_id,
            ) = self._get_stream_details(session_key, self.callerArgs.id)
            thousandeyes_client = ThousandEyesClient(
                session_key, thousandeyes_user, logger
            )
            _ = thousandeyes_client.delete_stream(
                thousandeyes_acc_group_id, thousandeyes_stream_id
            )  # noqa F841
            logger.info(
                f"Successfully deleted the ThousandEyes activity logs stream for input: {self.callerArgs.id}."
            )
            super().handleRemove(conf_info)
            logger.info(
                f"Successfully deleted the ThousandEyes activity logs stream input: {self.callerArgs.id}."
            )
        except Exception as e:
            logger.error(
                f"Error occurred while deleting the ThousandEyes activity logs stream input: {self.callerArgs.id}."
                f"Error: {e} {traceback.format_exc()}"
            )
            raise RestError(
                409,
                f"Error occurred while deleting the ThousandEyes activity logs stream input."
                f"Error: {e} Please check the logs.",
            )
