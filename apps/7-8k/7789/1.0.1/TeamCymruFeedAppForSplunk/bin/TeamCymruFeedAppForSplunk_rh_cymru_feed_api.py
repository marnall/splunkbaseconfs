import import_declare_test  # noqa

from splunktaucclib.rest_handler.endpoint import (
    field,
    validator,
    RestModel,
    DataInputModel,
)
from splunktaucclib.rest_handler import admin_external, util
from cymru_helpers.checkpoint_handler import check_point_delete_helper
from cymru_helpers.logger_manager import setup_logging
import splunk.rest as rest

util.remove_http_proxy_env_vars()
import logging
import os
logger = setup_logging("ta_team_cymru_feed_input_handling")
from import_declare_test import ta_name

try:
    from urllib import quote
except ImportError:
    from urllib.parse import quote


class CustomConfigHandlerFeedAPI(admin_external.AdminExternalHandler):
    """Manage the Rest Handler for Inputs."""

    def __init__(self, *args, **kwargs):
        """Init method of custom handler class."""
        admin_external.AdminExternalHandler.__init__(self, *args, **kwargs)

    def create_batch_stanza(self):
        """Create batch input stanza."""
        spool_path = os.path.join("$SPLUNK_HOME", "var", "spool", "splunk", self.callerArgs.id, "insights_*.jsonl.gz")
        batch_input_stanza = {
            "name": "batch://{}".format(
                spool_path
            ),
            "move_policy": "sinkhole",
            "crcSalt": "<SOURCE>",
            "disabled": "false",
            "index": self.payload.get("index", "default"),
            "sourcetype": "team_cymru_feed_api",
        }
        res, _ = rest.simpleRequest(
            "/servicesNS/nobody/{}/configs/conf-inputs".format(ta_name),
            self.getSessionKey(),
            postargs=batch_input_stanza,
            method="POST",
            rawResult=True,
            raiseAllErrors=True,
        )
        if res.status not in [201, 409]:
            raise Exception("Unable to create batch input")

    def reload_batch_input(self, session_key):
        """
        Reload the inputs present under data/inputs/monitor.

        :param session_key: session_key for Splunk Authentication
        """
        try:
            rest.simpleRequest(
                "/servicesNS/nobody/{}/data/inputs/monitor/_reload".format(ta_name),
                session_key,
                method="POST",
                raiseAllErrors=True,
            )
        except Exception as e:
            raise Exception("Error while reloading batch stanza: {}".format(e))

    def handleCreate(self, confInfo):
        """Handle creation of account in config file."""
        self.create_batch_stanza()
        self.reload_batch_input(self.getSessionKey())
        super(CustomConfigHandlerFeedAPI, self).handleCreate(confInfo)

    def handleRemove(self, conf_info):
        """
        Handles the delete operation.

        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        session_key = self.getSessionKey()
        try:
            input_name = self.callerArgs.id
            encoded_stanza = quote(
                "batch://{}/{}/insights_*.jsonl.gz".format(
                    SPOOL_DIR, self.callerArgs.id
                ),
                safe="",
            )
            rest.simpleRequest(
                "/servicesNS/nobody/{}/configs/conf-inputs/{}".format(
                    ta_name, encoded_stanza
                ),
                sessionKey=session_key,
                method="DELETE",
                getargs={"output_mode": "json"},
                raiseAllErrors=True,
            )
            check_point_delete_helper(session_key, f"cymru_feed_api_{input_name}")
            self.reload_batch_input(session_key)
            super(CustomConfigHandlerFeedAPI, self).handleRemove(conf_info)
            logger.info("Input {} successfully deleted.".format(input_name))
        except Exception as e:
            logger.error(
                "Error occurred during deletion for {} input \
                \nError: {}".format(
                    self.callerArgs.id, str(e)
                )
            )
            # Delete the input if there is a problem while deleting the checkpoint
            super(CustomConfigHandlerFeedAPI, self).handleRemove(conf_info)


fields = [
    field.RestField(
        "interval",
        required=True,
        encrypted=False,
        default="3600",
        validator=validator.Pattern(
            regex=r"""^(?:-1|\d+(?:\.\d+)?)$""",
        ),
    ),
    field.RestField(
        "index",
        required=False,
        encrypted=False,
        default="default",
        validator=validator.AllOf(
            validator.Pattern(
                regex=r"""^[a-zA-Z0-9][a-zA-Z0-9\\_\\-]*$""",
            ),
            validator.String(
                max_len=80,
                min_len=1,
            ),
        ),
    ),
    field.RestField(
        "account", required=True, encrypted=False, default=None, validator=None
    ),
    field.RestField(
        "api_type", required=True, encrypted=False, default=None, validator=None
    ),
    field.RestField("disabled", required=False, validator=None),
]

model = RestModel(fields, name=None)

endpoint = DataInputModel(
    "cymru_feed_api",
    model,
)


if __name__ == "__main__":
    logging.getLogger().addHandler(logging.NullHandler())
    admin_external.handle(
        endpoint,
        handler=CustomConfigHandlerFeedAPI,
    )
