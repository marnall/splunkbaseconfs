logger = log.get_logger(__file__)

util.remove_http_proxy_env_vars()

class CustomConfigMigrationHandler(ConfigMigrationHandler):
    """
    Manage the Rest Handler for server
    :param ConfigMigrationHandler: inhereting ConfigMigrationHandler
    """
    def handleRemove(self, conf_info):
        """
        Handles the delete operation
        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        # Read global account used in the input
        input_name = self.callerArgs.id
        input_type = "mandiant_security_validation_reporting"
        configs = cli.getConfStanza("inputs", "mandiant_security_validation_reporting://{}".format(input_name))
        acc_name = configs.get('mandiant_advantage_account')
        checkpoint_key = "mandiant_advantage_security_validation_{}_{}_checkpoint".format(acc_name, input_name)
        checkpoint_obj = ckpt.CheckpointBase(checkpoint_key, input_type)

        try:
            checkpoint_obj._delete()
            logger.info("Checkpoint deleted successfully for the Input: {}".format(input_name))
        except Exception:
            logger.error("Error occurred while deleting checkpoint. Error: {}".format(traceback.format_exc()))
        finally:
            super(CustomConfigMigrationHandler, self).handleRemove(conf_info)

special_fields = [
    field.RestField(
        'name',
        required=True,
        encrypted=False,
        default=None,
        validator=validator.AllOf(
            validator.Pattern(
                regex=r"""^[a-zA-Z]\w*$""", 
            ), 
            validator.String(
                max_len=100, 
                min_len=1, 
            )
        )
    )
]

fields = [
    field.RestField(
        'interval',
        required=True,
        encrypted=False,
        default='3600',
        validator=validator.AllOf(
            validator.Pattern(
                regex=r"""^((?:-1|\d+(?:\.\d+)?)|(([\*\d{1,2}\,\-\/]+\s){4}[\*\d{1,2}\,\-\/]+))$""", 
            ), 
            validator.Number(
                max_val=86400, 
                min_val=300, 
            )
        )
    ), 
    field.RestField(
        'index',
        required=True,
        encrypted=False,
        default='default',
        validator=validator.String(
            max_len=80, 
            min_len=1, 
        )
    ), 
    field.RestField(
        'mandiant_advantage_account',
        required=True,
        encrypted=False,
        default=None,
        validator=ValidateCorrectAccountValidator()
    ), 
    field.RestField(
        'delay_in_minutes',
        required=True,
        encrypted=False,
        default='120',
        validator=validator.AllOf(
            validator.String(
                max_len=8192, 
                min_len=0, 
            ), 
            validator.Number(
                max_val=1440, 
                min_val=5, 
            ), 
            validator.Pattern(
                regex=r"""^\-[1-9]\d*$|^\d*$""", 
            )
        )
    ), 

    field.RestField(
        'disabled',
        required=False,
        validator=None
    )

]
model = RestModel(fields, name=None, special_fields=special_fields)



endpoint = DataInputModel(
    'mandiant_security_validation_reporting',
    model,
)


if __name__ == '__main__':
    logging.getLogger().addHandler(logging.NullHandler())
    admin_external.handle(
        endpoint,
        handler=CustomConfigMigrationHandler,
    )
