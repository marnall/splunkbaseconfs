class CustomConfigMigrationHandler(ConfigMigrationHandler):
    """
    Manage the Rest Handler for server
    :param ConfigMigrationHandler: inhereting ConfigMigrationHandler
    """
    def delete_checkpoint(self, checkpoint_key):
        """Delete the checkpoint when user deletes input
        :return:
        """
        app_name = __file__.split(os.sep)[-3]
        base_url = "/servicesNS/nobody/{}/storage/collections/data/TA_mandiant_advantage_checkpointer/".format(
            app_name)
        session_key = self.getSessionKey()
        url = base_url + checkpoint_key
        try:
            _, _ = rest.simpleRequest(url, sessionKey=session_key, method='DELETE', getargs={"output_mode": "json"}, raiseAllErrors=True)
        except Exception:
            raise

    def handleRemove(self, conf_info):
        """
        Handles the delete operation
        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        input_name = self.callerArgs.id
        try:
            configs = utils.read_conf_file(self.getSessionKey(), "inputs", "mandiant_advantage_monitoring_alerts://{}".format(input_name))
            acc_name = configs.get('mandiant_advantage_account')
            checkpoint_key = "mandiant_advantage_monitoring_alerts_{}_{}_checkpoint".format(acc_name, input_name)
        except Exception as e:
            logger.error("message=Conf_read_exception | Following error occurred while reading configuration file: {}".format(e))

        try:
            self.delete_checkpoint(checkpoint_key)
            logger.info("message=checkpoint_deleted_successfully | Checkpoint deleted successfully for the Input: {}".format(input_name))
        except Exception:
            logger.error("message=checkpoint_deletion_error | Error occurred while deleting checkpoint. Error: {}".format(traceback.format_exc()))
        finally:
            super(CustomConfigMigrationHandler, self).handleRemove(conf_info)

