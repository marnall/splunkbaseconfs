import os
import logging
import traceback
import splunk.rest as rest
import import_declare_test    # noqa: F401
from import_declare_test import ta_name

from splunktaucclib.rest_handler.endpoint import (
    field,
    validator,
    RestModel,
    DataInputModel,
)
from splunktaucclib.rest_handler import admin_external, util
from splunktaucclib.rest_handler.error import RestError
from splunktaucclib.rest_handler.admin_external import AdminExternalHandler
from dataminr_utils import IntervalConvertor
from dataminr_validator import V4AccValidator
from log_helper import setup_logging


util.remove_http_proxy_env_vars()
logger = setup_logging(os.path.splitext(os.path.basename(__file__))[0].lower())


fields = [
    field.RestField(
        'interval',
        required=True,
        encrypted=False,
        default='300',
        validator=validator.Pattern(
            regex=r"""^\-[1-9]\d*$|^\d*$""",
        ),
        converter=IntervalConvertor()
    ),
    field.RestField(
        'index',
        required=True,
        encrypted=False,
        default='default',
        validator=validator.String(
            min_len=1,
            max_len=80,
        )
    ),
    field.RestField(
        'dataminr_account',
        required=True,
        encrypted=False,
        default=None,
        validator=V4AccValidator()
    ),
    field.RestField(
        'alert_type',
        required=True,
        encrypted=False,
        default='All',
        validator=None
    ),
    field.RestField(
        'lists_names',
        required=True,
        encrypted=False,
        default=None,
        validator=None
    ),
    field.RestField(
        'disabled',
        required=False,
        validator=None
    )

]
model = RestModel(fields, name=None)


endpoint = DataInputModel(
    'dataminr_api_v4',
    model,
)


class DataminrPulseAPIHandler(AdminExternalHandler):
    """Manage Dataminr API (Pulse) Data Input Details."""

    def __init__(self, *args, **kwargs):
        AdminExternalHandler.__init__(self, *args, **kwargs)

    @staticmethod
    def get_session_key(self):
        """Get session Key."""
        return self.getSessionKey()

    def handleRemove(self, confInfo):
        """
        Handle the delete operation.

        :param confInfo: Default parameter generated by UCC which is used to pass the response
        """
        logger.info(f"Deleting the checkpoint for Dataminr API input: {self.callerArgs.id}")
        session_key = self.getSessionKey()
        try:
            response_status, response_content = rest.simpleRequest(
                f"/servicesNS/nobody/{ta_name}/storage/collections/data/{ta_name}_checkpointer/{self.callerArgs.id}",
                sessionKey=session_key, method='DELETE', getargs={"output_mode": "json"}, raiseAllErrors=True)
            logger.info(f"Successfully deleted the checkpoint for Dataminr API input: {self.callerArgs.id}")
        except Exception as e:
            logger.error(
                f"Error occured while deleting the checkpoint for Dataminr API input: {self.callerArgs.id}."
                f"Error: {e}. {traceback.format_exc()}"
            )
        finally:
            super(DataminrPulseAPIHandler, self).handleRemove(confInfo)


if __name__ == '__main__':
    logging.getLogger().addHandler(logging.NullHandler())
    admin_external.handle(
        endpoint,
        handler=DataminrPulseAPIHandler,
    )
