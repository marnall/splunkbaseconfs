
import os
import traceback
import logging
import json
import splunk.rest as rest
import import_declare_test    # noqa: F401
from import_declare_test import ta_name

from splunktaucclib.rest_handler.endpoint import (
    field,
    validator,
    RestModel,
    DataInputModel,
)
from splunktaucclib.rest_handler.error import RestError
from splunktaucclib.rest_handler import admin_external, util
from splunktaucclib.rest_handler.admin_external import AdminExternalHandler
from dataminr_validator import HECTokenValidator
from dataminr_client import DataminrClient
from log_helper import setup_logging


util.remove_http_proxy_env_vars()
logger = setup_logging(os.path.splitext(os.path.basename(__file__))[0].lower())


fields = [
    field.RestField(
        'interval',
        required=True,
        encrypted=False,
        default='300',
        validator=validator.Pattern(
            regex=r"""^\-[1-9]\d*$|^\d*$""",
        )
    ),
    field.RestField(
        'dataminr_account',
        required=True,
        encrypted=False,
        default=None,
        validator=None
    ),
    field.RestField(
        'alert_type',
        required=True,
        encrypted=False,
        default='All',
        validator=None
    ),
    field.RestField(
        'lists_names',
        required=True,
        encrypted=False,
        default=None,
        validator=HECTokenValidator(),
    ),
    field.RestField(
        'hec_token',
        required=True,
        encrypted=False,
        default=None,
        validator=None,
    ),
    field.RestField(
        'dataminr_webhook_id',
        required=False,
        encrypted=False,
        default=None,
        validator=None
    ),
    field.RestField(
        'disabled',
        required=False,
        validator=None
    )
]
model = RestModel(fields, name=None)


endpoint = DataInputModel(
    'dataminr_webhook',
    model,
)


class DataminrWebhookHandler(AdminExternalHandler):
    """Manage Dataminr Webhook Data Input Details."""

    def __init__(self, *args, **kwargs):
        AdminExternalHandler.__init__(self, *args, **kwargs)

    @staticmethod
    def get_session_key(self):
        """Get session key."""
        return self.getSessionKey()

    def handleEdit(self, confInfo):
        """
        Handle the edit operation.

        :param confInfo: Default parameter generated by UCC which is used to pass the response
        """
        logger.info(f"Updating the Dataminr Webhook input: {self.callerArgs.id}")
        if self.payload.get("disabled"):
            logger.error("Cannot disable Dataminr Webhook Inputs. Please delete it if required.")
            raise RestError(409, "Cannot disable Dataminr Webhook Inputs. Please delete it if required.")
        logger.info(f"Successfully updated the Dataminr Webhook input: {self.callerArgs.id}")
        super(DataminrWebhookHandler, self).handleEdit(confInfo)

    def handleRemove(self, confInfo):
        """
        Handle the delete operation.

        :param confInfo: Default parameter generated by UCC which is used to pass the response
        """
        logger.info(f"Deleting the Dataminr Webhook input: {self.callerArgs.id}")
        try:
            session_key = self.getSessionKey()
            logger.info(f"Fetching the Dataminr account for input: {self.callerArgs.id}")
            response_status, response_content = rest.simpleRequest(
                f"/servicesNS/nobody/{ta_name}/data/inputs/dataminr_webhook/{self.callerArgs.id}",
                sessionKey=session_key, method='GET', getargs={"output_mode": "json"}, raiseAllErrors=True)
            response_content = json.loads(response_content)["entry"][0]["content"]
            dataminr_account = response_content["dataminr_account"]
            dataminr_webhook_id = response_content["dataminr_webhook_id"]
            logger.info(f"Dataminr account for input: {self.callerArgs.id} is : {dataminr_account}")
            dataminr_client = DataminrClient(session_key, dataminr_account)
            resp = dataminr_client.delete_dataminr_webhook(dataminr_webhook_id)
            super(DataminrWebhookHandler, self).handleRemove(confInfo)
            logger.info(f"Successfully deleted the Dataminr Webhook input: {self.callerArgs.id}")
        except Exception as e:
            logger.error(
                f"Error occured while deleting the Dataminr Webhook input: {self.callerArgs.id}."
                f"Error: {e}. {traceback.format_exc()}"
            )
            raise RestError(409, "Error occured while deleting the Dataminr Webhook input. Please check the logs.")


if __name__ == '__main__':
    logging.getLogger().addHandler(logging.NullHandler())
    admin_external.handle(
        endpoint,
        handler=DataminrWebhookHandler,
    )
