import ta_lansweeper_declare  # noqa:   401
import splunk.admin as admin
import traceback
import common.lansweeper_const as const

from splunktaucclib.rest_handler.error import RestError
from common.account_utils import update_workflow
from splunk_aoblib.rest_migration import ConfigMigrationHandler
from common.logger_manager import setup_logging

_LOGGER = setup_logging("ta_lansweeper_workflow_fields")


class WorkFlowFields(ConfigMigrationHandler):
    """REST Endpoint to update workflow actions fields."""

    def workflow_actions_updation(self):
        """Updates the workflow_actions.conf with given values for field."""
        message = "Error while updating fields for workflow action - {}. Error: {}"
        session_key = self.getSessionKey()
        try:
            update_workflow(session_key, const.WORKFLOW_IP,
                            self.payload.get(const.WORKFLOW_IP_FIELDS))
        except Exception as e:
            _LOGGER.info(message.format(const.WORKFLOW_IP_LABEL,
                                        traceback.format_exc()))
            raise RestError("500", message.format(const.WORKFLOW_IP_LABEL, str(e)))

        try:
            update_workflow(session_key, const.WORKFLOW_MAC,
                            self.payload.get(const.WORKFLOW_MAC_FIELDS))
        except Exception as e:
            _LOGGER.info(message.format(const.WORKFLOW_MAC_LABEL,
                                        traceback.format_exc()))
            raise RestError("500", message.format(const.WORKFLOW_MAC_LABEL, str(e)))

    def handleEdit(self, conf_info):
        """
        Handles the edit operation.

        :param conf_info: Default parameter generated by AOB which is used to pass the response
        :return:
        """
        self.workflow_actions_updation()

        super(WorkFlowFields, self).handleEdit(conf_info)

    def handleCreate(self, conf_info):
        """
        Handles the create operation.

        :param conf_info: Default parameter generated by AOB which is used to pass the response
        :return:
        """
        self.workflow_actions_updation()

        super(WorkFlowFields, self).handleCreate(conf_info)


if __name__ == "__main__":
    admin.init(WorkFlowFields, admin.CONTEXT_APP_AND_USER)
