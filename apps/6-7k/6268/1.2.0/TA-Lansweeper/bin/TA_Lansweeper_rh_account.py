
import ta_lansweeper_declare

from splunktaucclib.rest_handler.endpoint import (
    field,
    RestModel,
    SingleModel,
)
from splunktaucclib.rest_handler import admin_external, util
from splunk import admin
from splunk_aoblib.rest_migration import ConfigMigrationHandler
from solnlib.splunkenv import get_splunkd_access_info
from utils_account import ValidateAccount
from common.lansweeper_const import COLLECTION_NAME

util.remove_http_proxy_env_vars()


class AccountHandler(ConfigMigrationHandler):
    """
    Manage the Rest Handler for server
    :param ConfigMigrationHandler: inhereting ConfigMigrationHandler
    """

    def handleRemove(self, conf_info):
        """
        Handles the delete operation
        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        session_key = self.getSessionKey()
        try:
            scheme, host, port = get_splunkd_access_info()
            validator = ValidateAccount()
            service = validator.create_service(ta_lansweeper_declare.ta_name, host, port, session_key)
            if COLLECTION_NAME in service.kvstore:
                collection = service.kvstore[COLLECTION_NAME]
                collection.data.delete()
            super(ConfigMigrationHandler, self).handleRemove(conf_info)
        except Exception as e:
            raise admin.ArgValidationException("Error while deleting the sites associates with account : {}".format(e))


fields = [
    field.RestField(
        'token',
        required=True,
        encrypted=True,
        default=None,
        validator=ValidateAccount()
    )
]
model = RestModel(fields, name=None)


endpoint = SingleModel(
    'ta_lansweeper_account',
    model,
)


if __name__ == '__main__':
    admin_external.handle(
        endpoint,
        handler=AccountHandler,
    )
