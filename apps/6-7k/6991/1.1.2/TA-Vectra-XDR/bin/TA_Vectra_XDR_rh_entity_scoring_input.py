util.remove_http_proxy_env_vars()

import splunk.rest as rest
import os

import common.log as log
import common.utility as utils
logger = log.get_logger("TA_Vectra_XDR_rh_entity_scoring_input")

class CustomConfigMigrationHandler(ConfigMigrationHandler):
    """
    Manage the Rest Handler for server
    :param ConfigMigrationHandler: inhereting ConfigMigrationHandler
    """
    def delete_checkpoint(self, checkpoint_key):
        """Delete the checkpoint when user deletes input
        :return:
        """
        app_name = __file__.split(os.sep)[-3]
        base_url = "/servicesNS/nobody/{}/storage/collections/data/TA_Vectra_XDR_checkpointer/".format(
            app_name)
        session_key = self.getSessionKey()
        url = base_url + checkpoint_key
        try:
            _, _ = rest.simpleRequest(url, sessionKey=session_key, method='DELETE', getargs={"output_mode": "json"}, raiseAllErrors=True)
        except Exception:
            raise

    def handleRemove(self, conf_info):
        """
        Handles the delete operation
        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        input_name = self.callerArgs.id
        try:
            configs = utils.read_conf_file(self.getSessionKey(), "inputs", "entity_scoring_input://{}".format(input_name))
            acc_name = configs.get('vectra_xdr_account')
            checkpoint_key = "vectra_xdr_entity_scoring_{}-{}".format(acc_name, input_name)
        except Exception as e:
            logger.error(
                "input_name={} | message=Conf_read_exception | Following error occurred while reading configuration file: {}".format(input_name, e))

        try:
            self.delete_checkpoint(checkpoint_key)
            logger.info(
                "input_name={} | message=checkpoint_deleted_successfully | Checkpoint deleted successfully for the Input: {}".format(input_name, input_name))
        except Exception:
            logger.error("input_name={} | message=checkpoint_deletion_error | Error occurred while deleting checkpoint. Error: {}".format(input_name, traceback.format_exc()))
        finally:
            super(CustomConfigMigrationHandler, self).handleRemove(conf_info)



fields = [
    field.RestField(
        'interval',
        required=True,
        encrypted=False,
        default=60,
        validator=validator.Pattern(
            regex=r"""^\-[1-9]\d*$|^\d*$""", 
        )
    ), 
    field.RestField(
        'index',
        required=True,
        encrypted=False,
        default='default',
        validator=validator.String(
            min_len=1, 
            max_len=80, 
        )
    ), 
    field.RestField(
        'vectra_xdr_account',
        required=True,
        encrypted=False,
        default=None,
        validator=None
    ),
    field.RestField(
        'historical_data',
        required=False,
        default=False,
        validator=ValidateInputParams()
    ),
    field.RestField(
        'input_start_time',
        required=False,
        validator=None
    ),
    field.RestField(
        'disabled',
        required=False,
        validator=None
    )
]
model = RestModel(fields, name=None)



endpoint = DataInputModel(
    'entity_scoring_input',
    model,
)


if __name__ == '__main__':
    admin_external.handle(
        endpoint,
        handler=CustomConfigMigrationHandler,
    )
