import os
import sys
from typing import Any

sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", "lib"))

from splunklib.modularinput import Argument, Scheme

from ame.modinput_ame import AmeModularInput
from ame.utilities.MessageSearchLinkBuilder import build_trace_uuid_search_link


class AlertQueueConsumer(AmeModularInput):
    def get_scheme(self) -> Scheme:
        scheme = Scheme("AME alert processing")
        scheme.description = "This task processes the 'ame_alertqueue'"

        ## Optional settings
        # Run this script for all instances
        scheme.use_single_instance = False
        # External validation, if set to true, validate_input() will be run.
        scheme.use_external_validation = True

        # Add arguments to the scheme. Has to match with inputs.conf.spec
        dummy_argument = Argument("default")
        dummy_argument.data_type = Argument.data_type_string
        dummy_argument.description = "Unused Default Argument"
        dummy_argument.required_on_create = True
        scheme.add_argument(dummy_argument)

        return scheme

    def handle_stream_events(
        self,
        inputs: Any,  # noqa: ANN401
        ew: Any,  # noqa: ANN401
    ) -> None:
        try:
            index_cache_dataservice = self.service_factory.index_cache_dataservice
            event_service = self.service_factory.event_service
            alert_queue_service = self.service_factory.alertqueue_dataservice

            marked_for_deletion = []

            processed_alerts = 0
            failed_alerts = 0
            deleted_entries = 0

            for iteration in range(10):
                queued_alerts = alert_queue_service.all(limit=1000)

                if len(queued_alerts) == 0:
                    self.logger.info(
                        {
                            "action": "no_alerts_found",
                            "iteration": iteration,
                        }
                    )
                    break
                self.logger.info(
                    {
                        "action": "found_alerts",
                        "count": len(queued_alerts),
                        "iteration": iteration,
                    }
                )

                for loop_index, alert_queue_entry in enumerate(queued_alerts):
                    try:
                        self.logger.debug(
                            {
                                "action": "processing_alert",
                                "alert_queue_entry": alert_queue_entry,
                                "loop_index": loop_index,
                            }
                        )
                        created_event = event_service.create(alert_queue_entry)
                        self.logger.debug(
                            {
                                "action": "created_event",
                                "created_event": created_event,
                                "loop_index": loop_index,
                            }
                        )
                        processed_alerts += 1
                    except Exception as exc:
                        self.logger.exception(
                            {
                                "action": "alert_queue_consumer_exception",
                                "exception": exc,
                                "index": loop_index,
                                "alert_queue_entry": alert_queue_entry,
                            }
                        )
                        self.sdk_wrapper.send_message(
                            name=f"fail_consume_alertqueue_{alert_queue_entry.search_name}",
                            value=f"Failed to create / append alertqueue entry generated by search '{alert_queue_entry.search_name}'. Please check the AME logs for a detailed error message: {build_trace_uuid_search_link(self.uuid)}",
                            severity="error",
                        )
                        failed_alerts += 1
                    finally:
                        marked_for_deletion.append(alert_queue_entry.key)

                alert_queue_service.delete_bulk(instances=marked_for_deletion, hard=True)
                deleted_entries += len(marked_for_deletion)
                marked_for_deletion = []

            self.logger.info(
                {
                    "action": "processed_alerts",
                    "count": processed_alerts,
                    "failed": failed_alerts,
                    "deleted": deleted_entries,
                }
            )

            try:
                self.logger.debug(
                    {
                        "action": "delete_obsolete_entries",
                        "status": "started",
                    }
                )
                index_cache_dataservice.delete_obsolete_entries()
                self.logger.debug(
                    {
                        "action": "delete_obsolete_entries",
                        "status": "success",
                    }
                )
            except Exception as exc:
                self.logger.exception(
                    {
                        "action": "index_cache_management_exception",
                        "exception": exc,
                    }
                )

        except Exception as exc:
            self.logger.exception(
                {
                    "action": "alert_queue_consumer_exception",
                    "exception": exc,
                }
            )


if __name__ == "__main__":
    sys.exit(AlertQueueConsumer().run(sys.argv))
