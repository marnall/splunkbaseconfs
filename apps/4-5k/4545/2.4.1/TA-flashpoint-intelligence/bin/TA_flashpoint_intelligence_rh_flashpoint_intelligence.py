class RemoveRedundantParam(validator.Validator):

    def validate(self, value, data):
        if data.get('type') != 'compromised_credentials':
            data.pop('collect_plain_text_password', None)
            data.pop('is_fresh', None)
            data.pop('password_complexity_has_lowercase', None)
            data.pop('password_complexity_has_number', None)
            data.pop('password_complexity_has_symbol', None)
            data.pop('password_complexity_has_uppercase', None)
            data.pop('password_complexity_length', None)
        if data.get('type') != 'ransomware':
            data.pop('collect_enrichments', None)

        return True


class CustomConfigMigrationHandler(ConfigMigrationHandler):
    """
    Manage the Rest Handler for server
    """

    def delete_checkpoint(self):
        """Delete the checkpoint when user deletes input
        :return:
        """
        app_name = __file__.split(os.sep)[-3]
        base_url = "/servicesNS/nobody/{}/storage/collections/data/TA_flashpoint_intelligence_checkpointer/".format(
            app_name)
        session_key = self.getSessionKey()
        url = base_url + str(self.callerArgs.id)
        try:
            _, _ = rest.simpleRequest(url,
                                      sessionKey=session_key, method='DELETE', getargs={"output_mode": "json"}, raiseAllErrors=True)
            logger.info('Successfully deleted checkpoint for input "{}".'.format(self.callerArgs.id))
        except Exception as e:
            logger.error("Error while deleting checkpoint for {} input. \n Error: {}".format(str(
                self.callerArgs.id), str(e)))

    def handleRemove(self, confInfo):
        """
            Handles the delete operation
            :param confInfo: Default parameter generated by AOB which is used to pass the response
            :return:
        """
        self.delete_checkpoint()
        super(CustomConfigMigrationHandler, self).handleRemove(confInfo)


