import ta_dell_ecs_declare
from splunktaucclib.rest_handler.endpoint import (
    field,
    validator,
    RestModel,
    DataInputModel,
)
import splunk.rest as rest
from splunktaucclib.rest_handler import admin_external, util
from splunk_aoblib.rest_migration import ConfigMigrationHandler
from splunktaucclib.rest_handler.error import RestError
from interval_validation import IntervalValidator
import os
import json
my_app = __file__.split(os.sep)[-3]

util.remove_http_proxy_env_vars()


class CustomConfigMigrationHandler(ConfigMigrationHandler):
    """
    Manage the Rest Handler for server
    :param ConfigMigrationHandler: inhereting ConfigMigrationHandler
    """

    def handleRemove(self, conf_info):
        """ 
        Handles the delete operation
        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        session_key = self.getSessionKey()
        try:
            response_status, response_content = rest.simpleRequest(
                "/servicesNS/nobody/" + str(my_app) + "/storage/collections/data/" + str(my_app.replace('-', '_')) +
                "_checkpointer/" + str(self.callerArgs.id + ":dell:events"),
                sessionKey=session_key, method='DELETE', getargs={"output_mode": "json"},
                raiseAllErrors=True)
            response_status, response_content = rest.simpleRequest(
                "/servicesNS/nobody/" + str(my_app) + "/storage/collections/data/" + str(my_app.replace('-', '_')) +
                "_checkpointer/" + str(self.callerArgs.id + ":dell:alerts"),
                sessionKey=session_key, method='DELETE', getargs={"output_mode": "json"},
                raiseAllErrors=True)

            super(CustomConfigMigrationHandler, self).handleRemove(conf_info)

        except Exception:
            # Delete the input if there is a problem while deleting the checkpoint
            super(CustomConfigMigrationHandler, self).handleRemove(conf_info)

    def handleEdit(self, conf_info):
        """
        Handles the edit operation
        :param conf_info: Default parameter generated by AOB which is used to pass the response
        """
        session_key = self.getSessionKey()
        start_time = self.payload.get("start_time")
        if start_time:
            input_name = "dell_ecs_input://" + str(self.callerArgs.id)

            try:
                response_status, response_content = rest.simpleRequest(
                    "/servicesNS/nobody/my_app/configs/conf-inputs",
                    sessionKey=session_key, getargs={"output_mode": "json"}, raiseAllErrors=True)
                res = json.loads(response_content)
                for inputs in res['entry']:
                    if inputs['name'] == input_name and inputs['content']['start_time'] != start_time:
                        raise Exception("Cannot modify the Start Date field once it is configured.")
            except Exception as e:
                raise RestError(409, "Cannot modify the Start Date field once it is configured.")
        super(CustomConfigMigrationHandler, self).handleEdit(conf_info)


fields = [
    field.RestField(
        'interval',
        required=True,
        encrypted=False,
        default='300',
        validator=IntervalValidator()
    ),
    field.RestField(
        'index',
        required=True,
        encrypted=False,
        default='default',
        validator=validator.String(
            max_len=80,
            min_len=1,
        )
    ),
    field.RestField(
        'global_account',
        required=True,
        encrypted=False,
        default=None,
        validator=None
    ),
    field.RestField(
        'start_time',
        required=False,
        encrypted=False,
        default=None,
        validator=validator.String(
            max_len=8192,
            min_len=0,
        )
    ),

    field.RestField(
        'disabled',
        required=False,
        validator=None
    )

]
model = RestModel(fields, name=None)


endpoint = DataInputModel(
    'dell_ecs_input',
    model,
)

if __name__ == '__main__':
    admin_external.handle(
        endpoint,
        handler=CustomConfigMigrationHandler,
    )
