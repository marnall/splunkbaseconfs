
import ta_box_shield_declare
from splunktaucclib.rest_handler.endpoint import (
    field,
    validator,
    RestModel,
    DataInputModel,
)
import splunk.rest as rest
from splunktaucclib.rest_handler import admin_external, util
from splunk_aoblib.rest_migration import ConfigMigrationHandler
from splunktaucclib.rest_handler.error import RestError
from utility import setup_logger
import os
import json
my_app = __file__.split(os.sep)[-3]
util.remove_http_proxy_env_vars()
logger = setup_logger("ta_box_shield_box_shield_validation")

class CustomConfigMigrationHandler(ConfigMigrationHandler):
    """
    Manage the Rest Handler for server
    :param ConfigMigrationHandler: inhereting ConfigMigrationHandler
    """
    
    def checkpoint_delete_helper(self, sourcetype, session_key):
        """
        :param sourcetype: sourcetype to delete the checkpoint
        :session_key: session key for authenticate the requests
        """
        response_status, response_content = rest.simpleRequest("/servicesNS/nobody/" + str(my_app) + "/storage/collections/data/" + str(my_app.replace(
            '-', '_')) + "_checkpointer/" + str(self.callerArgs.id + str(sourcetype)), sessionKey=session_key, method='DELETE', getargs={"output_mode": "json"}, raiseAllErrors=True)


    def handleRemove(self, conf_info):
        """ 
        Handles the delete operation
        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        session_key = self.getSessionKey()
        try:
            # Commenting the code to prevent the removing checkpoint mechanism of smart access policy.
            # sourcetypes = [":box:shield:alerts",":box:shield:classification", ":box:shield:smartaccess:policy"]
            sourcetypes = [":box:shield:alerts",":box:shield:classification"]
            for sourcetype in sourcetypes:
                self.checkpoint_delete_helper(sourcetype, session_key)
            
            super(CustomConfigMigrationHandler, self).handleRemove(conf_info)
            
        except Exception as e:
            logger.error("Error occurred while deleting checkpoint for {} input \n Error: {}".format(self.callerArgs.id,str(e)))
            # Delete the input if there is a problem while deleting the checkpoint
            super(CustomConfigMigrationHandler, self).handleRemove(conf_info)

    def handleEdit(self, conf_info):
        """
        Handles the edit operation
        :param conf_info: Default parameter generated by AOB which is used to pass the response
        """
        session_key = self.getSessionKey()
        start_time = self.payload.get("start_time")
        input_name = "box_shield_input://" + str(self.callerArgs.id)
        if not start_time is None:     
            try:
                response_status, response_content = rest.simpleRequest(
                    "/servicesNS/nobody/"+ str(my_app) + "/configs/conf-inputs", sessionKey=session_key, getargs={"output_mode": "json"}, raiseAllErrors=True)
                res = json.loads(response_content)
                for inputs in res['entry']:
                        if inputs['name'] == input_name and inputs['content'].get('start_time','') != start_time:
                            raise Exception("Cannot modify the Start Time field once it is configured.")
            except Exception as e:
                logger.error("Error occurred while changing the Start Time for {} \nError : {}".format(self.callerArgs.id,e))
                raise RestError(409, "Cannot modify the Start Time field once it is configured.")
        super(CustomConfigMigrationHandler, self).handleEdit(conf_info)


fields = [
    field.RestField(
        'interval',
        required=True,
        encrypted=False,
        default=60,
        validator=validator.Pattern(
            regex=r"""^\-[1-9]\d*$|^\d*$""", 
        )
    ), 
    field.RestField(
        'index',
        required=True,
        encrypted=False,
        default='default',
        validator=validator.String(
            min_len=1, 
            max_len=80, 
        )
    ), 
    field.RestField(
        'box_account',
        required=True,
        encrypted=False,
        default=None,
        validator=None
    ), 
    field.RestField(
        'start_time',
        required=False,
        encrypted=False,
        default=None,
        validator=validator.String(
            min_len=0, 
            max_len=8192, 
        )
    ), 

    field.RestField(
        'disabled',
        required=False,
        validator=None
    )

]
model = RestModel(fields, name=None)



endpoint = DataInputModel(
    'box_shield_input',
    model,
)


if __name__ == '__main__':
    admin_external.handle(
        endpoint,
        handler=CustomConfigMigrationHandler,
    )
