
import ta_pps_ondemand_declare
from splunktaucclib.rest_handler.endpoint import (
    field,
    validator,
    RestModel,
    DataInputModel,
)
import splunk.rest as rest
from splunktaucclib.rest_handler import admin_external, util
from splunk_aoblib.rest_migration import ConfigMigrationHandler
import logger_manager

util.remove_http_proxy_env_vars()
_LOGGER = logger_manager.setup_logging("ta_pps_ondemand_rh_message")
app_name = ta_pps_ondemand_declare.ta_name

class CustomConfigMigrationHandlerMessage(ConfigMigrationHandler):
    '''
    Manage the Rest Handler for Inputs
    '''
    def check_point_delete_helper(self, session_key):
        response_status, response_content = rest.simpleRequest("/servicesNS/nobody/" + str(app_name) + "/storage/collections/data/" + str(app_name.replace(
            '-', '_')) + "_checkpointer/" + str(self.callerArgs.id), sessionKey=session_key, method='DELETE', getargs={"output_mode": "json"}, raiseAllErrors=True)


    def handleRemove(self, conf_info):
        """ 
        Handles the delete operation
        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        session_key = self.getSessionKey()
        try:
            self.check_point_delete_helper(session_key)        
            super(CustomConfigMigrationHandlerMessage, self).handleRemove(conf_info)
        except Exception as e:
            _LOGGER.error("Error occurred while deleting checkpoint for {} input \nError: {}".format(self.callerArgs.id, str(e)))
            # Delete the input if there is a problem while deleting the checkpoint
            super(CustomConfigMigrationHandlerMessage, self).handleRemove(conf_info)

fields = [
    field.RestField(
        'interval',
        required=True,
        encrypted=False,
        default=300,
        validator=validator.Pattern(
            regex=r"""^\-[1-9]\d*$|^\d*$""", 
        )
    ), 
    field.RestField(
        'index',
        required=True,
        encrypted=False,
        default='default',
        validator=validator.String(
            min_len=1, 
            max_len=80, 
        )
    ), 
    field.RestField(
        'global_account',
        required=True,
        encrypted=False,
        default=None,
        validator=None
    ), 
    field.RestField(
        'websocket_ping_interval',
        required=True,
        default=180,
        validator=None
    ),
    field.RestField(
        'enforce_sincetime',
        required=False,
        default=None,
        validator=None
    ),
    field.RestField(
        'disabled',
        required=False,
        validator=None
    )

]
model = RestModel(fields, name=None)



endpoint = DataInputModel(
    'proofpoint_message_log',
    model,
)


if __name__ == '__main__':
    admin_external.handle(
        endpoint,
        handler=CustomConfigMigrationHandlerMessage,
    )
