# noinspection PyUnusedLocal
def validate_input(helper,     # type: ModInputtrustar_splunk_es_ingest_iocs
                   definition  # type: ValidationDefinition
                   ):
    """
    Example of definition.params

    {
      "global_account": "SomeUser",
      "enclave_ids": "d609113c-0d46-4c98-8ebe-cda3b17988a8,953b0893-ce4b-436c-ac9b-f25a08950b40",
      "ioc_types": "IP~CIDR_BLOCK~URL~EMAIL_ADDRESS~MD5~SHA1~SHA256~MALWARE~SOFTWARE~CVE~REGISTRY_KEY~BITCOIN_ADDRESS",
      "index": "default",
      "tags_all_required": null,
      "interval": "300",
      "expiration_days": "30"
    }

    Example of definition.metadata

    {
      "session_key": [REDACTED - this is a 118-character string with upper & lowercase letters, digits, and underscores.],
      "server_uri": "https://127.0.0.1:8089",
      "server_host": "ip-10-0-0-235.ec2.internal",
      "name": "NameOfConfiguration",
      "checkpoint_dir": "/Applications/Splunk/var/lib/splunk/modinputs/trustar_splunk_es_modinput"
    }

    """

    # validate interval is an integer
    interval = definition.parameters.get('interval')
    try:
        int(interval)
    except ValueError:
        raise ValueError("Interval parameter {} is not an integer.".format(interval))

    # validate expiration_days is an integer
    expiration_days = definition.parameters.get('expiration_days')
    try:
        int(expiration_days)
    except ValueError:
        raise ValueError("Expiration Days parameter {} is not an integer.".format(expiration_days))


