import ta_armis_declare
import os

from splunktaucclib.rest_handler.endpoint import (
    field,
    validator,
    RestModel,
    DataInputModel,
)
import traceback
import splunk.rest as rest
from splunktaucclib.rest_handler import admin_external, util
from splunk_aoblib.rest_migration import ConfigMigrationHandler
import armis_utils as utils
from log_manager import setup_logging

logger = setup_logging("TA_armis_rh_armis_api_alerts")

class CustomConfigMigrationHandler(ConfigMigrationHandler):
    """
    Manage the Rest Handler for server
    :param ConfigMigrationHandler: inhereting ConfigMigrationHandler
    """
    def delete_checkpoint(self, checkpoint_key):
        """Delete the checkpoint when user deletes input
        :return:
        """
        base_url = "/servicesNS/nobody/TA-armis/storage/collections/data/TA_armis_checkpointer/"
        session_key = self.getSessionKey()
        url = base_url + checkpoint_key
        try:
            _, _ = rest.simpleRequest(url, sessionKey=session_key, method='DELETE', getargs={"output_mode": "json"}, raiseAllErrors=True)
        except Exception:
            raise

    def handleCreate(self, conf_info):
        """Handles lookback days value initialization."""
        try:
            lookback_days = int(self.payload.get('lookback_days'))
            if lookback_days > 90:
                logger.info("Lookback days value is greater than 90 : {}. defaulting to 90 days".format(lookback_days))
                self.payload['lookback_days'] = "90"
                
        except Exception as e:
            logger.error(
                "Following error occurred while creating an input: {}".format(e))
        finally:
            super(CustomConfigMigrationHandler, self).handleCreate(conf_info)

    def handleRemove(self, conf_info):
        """
        Handles the delete operation
        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        input_name = self.callerArgs.id
        try:
            configs = utils.read_conf_file(self.getSessionKey(), "inputs", "armis_api_alerts://{}".format(input_name))
            acc_name = configs.get('global_account')
            checkpoint_key = "armis_alerts_{}-{}".format(acc_name, input_name)
        except Exception as e:
            logger.error(
                "input_name={} | message=Conf_read_exception | Following error occurred while reading configuration file: {}".format(input_name, e))

        try:
            self.delete_checkpoint(checkpoint_key)
            logger.info(
                "input_name={} | message=checkpoint_deleted_successfully | Checkpoint deleted successfully for the Input: {}".format(input_name, input_name))
        except Exception:
            logger.error("input_name={} | message=checkpoint_deletion_error | Error occurred while deleting checkpoint. Error: {}".format(input_name, traceback.format_exc()))
        finally:
            super(CustomConfigMigrationHandler, self).handleRemove(conf_info)


fields = [
    field.RestField(
        'interval',
        required=True,
        encrypted=False,
        default=3600,
        validator=validator.Pattern(
            regex=r"""^\-[1-9]\d*$|^\d*$""", 
        )
    ),
    field.RestField(
        'index',
        required=True,
        encrypted=False,
        default='default',
        validator=validator.String(
            min_len=1,
            max_len=80,
        )
    ),
    field.RestField(
        'global_account',
        required=True,
        encrypted=False,
        default=None,
        validator=None
    ),
    field.RestField(
        'lookback_days',
        required=True,
        encrypted=False,
        default=90,
        validator=None
    ),
    field.RestField(
        'disabled',
        required=False,
        validator=None
    )

]
model = RestModel(fields, name=None)

endpoint = DataInputModel(
    'armis_api_alerts',
    model,
)


if __name__ == '__main__':
    admin_external.handle(
        endpoint,
        handler=CustomConfigMigrationHandler,
    )
