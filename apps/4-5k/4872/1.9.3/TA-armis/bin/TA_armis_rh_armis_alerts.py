import ta_armis_declare
import os

from splunktaucclib.rest_handler.endpoint import (
    field,
    validator,
    RestModel,
    DataInputModel,
)
import splunk.rest as rest
from splunktaucclib.rest_handler import admin_external, util
from splunk_aoblib.rest_migration import ConfigMigrationHandler

from armis_utils import IntervalValidator

util.remove_http_proxy_env_vars()
my_app = __file__.split(os.sep)[-3]


class CustomConfigMigrationHandler(ConfigMigrationHandler):
    """Manage the Rest Handler for Inputs."""

    def check_point_delete_helper(self, session_key):
        response_status, response_content = rest.simpleRequest("/servicesNS/nobody/" + str(my_app) + "/storage/collections/data/" + str(my_app.replace(
            '-', '_')) + "_checkpointer/" + str(self.callerArgs.id), sessionKey=session_key, method='DELETE', getargs={"output_mode": "json"}, raiseAllErrors=True)

    def handleRemove(self, conf_info):
        """
        Handles the delete operation.

        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        session_key = self.getSessionKey()
        try:
            self.check_point_delete_helper(session_key)
        except Exception:
            pass
        finally:
            # Delete the input if there is a problem while deleting the checkpoint
            super(CustomConfigMigrationHandler, self).handleRemove(conf_info)


fields = [
    field.RestField(
        'interval',
        required=True,
        encrypted=False,
        default=3600,
        validator=IntervalValidator()
    ),
    field.RestField(
        'index',
        required=True,
        encrypted=False,
        default='default',
        validator=validator.String(
            min_len=1,
            max_len=80,
        )
    ),
    field.RestField(
        'global_account',
        required=True,
        encrypted=False,
        default=None,
        validator=None
    ),
    field.RestField(
        'armis_index',
        required=True,
        encrypted=False,
        default='main',
        validator=validator.String(
            min_len=1,
            max_len=80,
        )
    ),
    field.RestField(
        'disabled',
        required=False,
        validator=None
    )

]
model = RestModel(fields, name=None)

endpoint = DataInputModel(
    'armis_alerts',
    model,
)


if __name__ == '__main__':
    admin_external.handle(
        endpoint,
        handler=CustomConfigMigrationHandler,
    )
