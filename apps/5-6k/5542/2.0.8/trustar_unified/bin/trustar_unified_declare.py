"""
This module is used to filter and reload PATH.
This file is generated by Splunk add-on builder

*** This file has been modified by TruSTAR to allow TruSTAR to isolate its
code and dependencies into a separate directory ("ts_spl_unified_pyN")
and and that directory to PATH.

Previously, TruSTAR would pip install --target its package and the package's
dependencies into the appropriate aob_pyN directories during build, but decided
to separate them.

"""

import sys
import re
from os.path import join, exists, dirname
from os.path import realpath

def running_in_ide():
    """ True if running in IDE, False if not.
    Do not want this moodule adjusting paths if executing tests in IDE.
    In IDE, paths are handled by .pth files, which are created by
    the dev_setup.sh script. """

    trace_fn = None
    try:
        trace_fn = sys.gettrace()

    except:
        return False

    if not trace_fn:
        return False

    return True

def get_dirname(prefix):
    """ Gets the path for the correct version of this dir, based on which
    interpreter major version is being used. """
    if sys.version_info[0] < 3:
        major_ver = "2"
    else:
        major_ver = "3"
    name_ = "{}_py{}".format(prefix, major_ver)
    return name_

def update_path():
    ta_name = 'trustar_unified'

    pattern = re.compile(r"[\\/]etc[\\/]apps[\\/][^\\/]+[\\/]bin[\\/]?$")
    paths_ = [path for path in sys.path if not pattern.search(path) or ta_name in path]

    lib_ = join(dirname(dirname(__file__)), "lib")
    new_paths = [join(dirname(__file__), "trustar_unified", get_dirname("aob")),  # AOB libs.
                 join(dirname(__file__), "trustar_unified"),  # AOB puts logging & modalert helpers here, not version-specific.
                 dirname(__file__),
                 join(lib_, get_dirname("ts_src")),  # contains "ts_spl_unified" library
                 join(lib_, get_dirname("ts_deps"))]    # dependencies of "ts_spl_unified" library.


    # dependencies we need during dev but can't be in prod artifact.
    # Ex:  "pydevd-pycharm~=202.7660.27"  for remote-debugging the process from
    # our dev splunk hosts.
    #
    # This dir will be present in dev artifact, but not in shipped artifact.
    ts_dev_deps = get_dirname("dev_deps")
    if exists(ts_dev_deps):
        new_paths.append(ts_dev_deps)

    new_paths.extend(paths_)
    sys.path = new_paths


if not running_in_ide():
    update_path()
