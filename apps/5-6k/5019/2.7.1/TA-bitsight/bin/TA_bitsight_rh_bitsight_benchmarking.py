util.remove_http_proxy_env_vars()

logger = setup_logging(os.path.splitext(os.path.basename(__file__))[0].lower())

my_app = 'TA-bitsight'


class CustomConfigMigrationHandler(ConfigMigrationHandler):
    """
    Manage the Rest Handler for server.

    :param ConfigMigrationHandler: inhereting ConfigMigrationHandler
    """

    def check_point_delete_helper(self, ckpt_key, input_name):
        """
        Helper method to delete checkpoint using Splunk REST API.

        :param session_key: Session Key
        :param ckpt_key: Checkpoint Key
        """
        session_key = self.getSessionKey()
        ckpt_url = "/servicesNS/nobody/{}/storage/collections/data/{}_checkpointer/{}".format(my_app, my_app.replace(
            '-', '_'), ckpt_key)
        try:
            rest.simpleRequest(ckpt_url, sessionKey=session_key, method='DELETE',
                               getargs={"output_mode": "json"}, raiseAllErrors=True)
        except Exception:
            # Do nothing as the checkpoint doesn't exist
            return

    def handleRemove(self, conf_info):
        """
        Handles the delete operation.

        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        # Read global account used in the input
        input_name = self.callerArgs.id
        configs = cli.getConfStanza("inputs", "bitsight_benchmarking://{}".format(input_name))
        if not is_true(configs.get('skip_checkpoint', 0)):
            splunk_session_key = self.getSessionKey()
            bsobject = BitsightCompanyGuidMapper(splunk_session_key, "benchmarking")
            try:
                response = bsobject.get_company_tree()
                if response.status_code != 200 and response.status_code != 201:
                    raise BitsightException(
                        "Not able to get list of companies . Response Code : {}"
                        "- Response Error : {}".format(response.status_code, response.text)
                    )
                companies_list = (json.loads(response.content)).get('results')
            except TypeError:
                message = "Please configure the BitSight API URL and/or BitSight API Token before creating input."
                logger.error(message)
            try:
                self.remove_ckpt(configs, companies_list, input_name)
            except Exception as e:
                logger.error("[CustomAdminExternalHandler] [handleRemove] Exception in reading input, error:" + str(e))
                raise RestError(409, e)
            logger.info("Checkpoints deleted successfully for the Input: [{}]".format(input_name))
        super(CustomConfigMigrationHandler, self).handleRemove(conf_info)

    def remove_ckpt(self, input_info, companies_list, input_name):
        """
        Helper method to remove checkpoint.

        :param input_info: Dictonary of field-value pairs of current input to be deleted.
        :param companies_list: List of all companies.
        :param input_name: Name of current input.
        """
        selected_companies = input_info['company_tree_multiselect']
        checkpoints_list = []
        if 'All' in selected_companies:
            selected_companies = [x.get('name') for x in companies_list]
        if not isinstance(selected_companies, list):
            selected_companies = list(selected_companies.split('|'))
        for endpoint in ENDPOINT_DISPATHCER.keys():
            if endpoint == 'findings':
                for x in ['Diligence', 'Compromised_Systems', 'User_Behavior']:
                    checkpoints_list.extend(['benchmarking_{}_{}_{}_{}'.format(input_name, endpoint, comp, x)
                                             for comp in selected_companies])
            else:
                checkpoints_list.extend([
                    'benchmarking_{}_{}_{}'.format(input_name, endpoint, comp)
                    for comp in selected_companies
                ])
        for ckpt in checkpoints_list:
            try:
                self.check_point_delete_helper(ckpt, input_name)
            except Exception:
                logger.error("Error occurred while deleting checkpoint [{}]. Error: {}".format(
                    ckpt, traceback.format_exc()))


special_fields = [
    field.RestField(
        'name',
        required=True,
        encrypted=False,
        default=None,
        validator=validator.AllOf(
            validator.Pattern(
                regex=r"""^[a-zA-Z]\w*$""",
            ),
            validator.String(
                max_len=100,
                min_len=1,
            )
        )
    )
]

fields = [
    field.RestField(
        'interval',
        required=True,
        encrypted=False,
        default=86400,
        validator=validator.AllOf(
            validator.Pattern(
                regex=r"""^((?:-1|\d+(?:\.\d+)?)|(([\*\d{1,2}\,\-\/]+\s){4}[\*\d{1,2}\,\-\/]+))$""",
            ),
            validator.Number(
                max_val=14515200,
                min_val=300,
            )
        )
    ),
    field.RestField(
        'index',
        required=True,
        encrypted=False,
        default='default',
        validator=BitsightDateValidator()
    ),
    field.RestField(
        'start_date',
        required=False,
        encrypted=False,
        default='',
        validator=BitsightAccountValidator()
    ),
    field.RestField(
        'company_tree_multiselect',
        required=True,
        encrypted=False,
        validator=BitsightBenchmarkingCompanyGuidManager()
    ),
    field.RestField(
        'skip_checkpoint',
        required=False,
        encrypted=False,
        default=None,
        validator=None
    ),
    field.RestField(
        'edit_flag',
        required=False,
        encrypted=False,
        default=None,
        validator=None
    ),
    field.RestField(
        'disabled',
        required=False,
        validator=None
    )

]
model = RestModel(fields, name=None, special_fields=special_fields)


endpoint = DataInputModel(
    'bitsight_benchmarking',
    model,
)


if __name__ == '__main__':
    admin_external.handle(
        endpoint,
        handler=CustomConfigMigrationHandler,
    )
