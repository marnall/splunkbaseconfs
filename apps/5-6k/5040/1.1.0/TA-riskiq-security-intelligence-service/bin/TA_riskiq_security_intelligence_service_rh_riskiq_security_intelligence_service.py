"""The rh file for the input RiskIQ Security Intelligence Service."""

import ta_riskiq_security_intelligence_service_declare

from splunktaucclib.rest_handler.endpoint import (
    field,
    validator,
    RestModel,
    DataInputModel,
)
from splunktaucclib.rest_handler import admin_external, util
from splunk_aoblib.rest_migration import ConfigMigrationHandler
from splunk import rest

try:
    from urllib import quote
except ImportError:
    from urllib.parse import quote

from riskiqsis_input_validation import InputValidator
from riskiqsis_interval_validation import IntervalValidator
from riskiqsis_utils import APP_NAME, reload_batch_input, INPUT_NAME, get_input_checkpoint_keys
from riskiqsis_account_utils import DATATYPE_SOURCETYPE_MAPPING, BATCH_STANZA_PATH, BATCH_STANZA_PREFIX
from setup_logger import setup_logging

logger = setup_logging("ta_riskiq_security_intelligence_service_riskiq_security_intelligence_service")

util.remove_http_proxy_env_vars()

class CustomConfigMigrationHandler(ConfigMigrationHandler):
    """
    Manage the Rest Handler for server.

    :param ConfigMigrationHandler: inhereting ConfigMigrationHandler
    """

    def handleCreate(self, confInfo):
        """Handle creation of account in config file."""
        payload = self.payload
        input_name = self.callerArgs.id
        data_type = payload.get('data_type')
        batch_input_stanza = {
                "name": BATCH_STANZA_PREFIX.format(BATCH_STANZA_PATH.format(input_name)),
                "move_policy": "sinkhole",
                "disabled": 0,
                "crcSalt": "<SOURCE>",
                "index": self.payload.get('index', "default"),
                "sourcetype": DATATYPE_SOURCETYPE_MAPPING[data_type]
            }

        res, _ = rest.simpleRequest(
            "/servicesNS/nobody/{}/configs/conf-inputs".format(APP_NAME),
            self.getSessionKey(),
            postargs=batch_input_stanza,
            method="POST",
            rawResult=True,
            raiseAllErrors=True,
        )
        if res.status not in [201, 409]:
            raise Exception("Unable to create batch input for {}".format(INPUT_NAME))

        reload_batch_input(self.getSessionKey())

        super(CustomConfigMigrationHandler, self).handleCreate(confInfo)

    def check_point_delete_helper(self, ckpt_key):
        """
        Helper method to delete checkpoint using Splunk REST API.

        :param session_key: Session Key
        :param ckpt_key: Checkpoint Key
        """
        session_key = self.getSessionKey()
        ckpt_url = "/servicesNS/nobody/{}/storage/collections/data/{}_checkpointer/".format(
            APP_NAME, APP_NAME.replace("-", "_")
        )
        keys = get_input_checkpoint_keys(session_key, ckpt_key)
        try:
            for key in keys:
                rest.simpleRequest(
                    ckpt_url+key,
                    sessionKey=session_key,
                    method="DELETE",
                    getargs={"output_mode": "json"},
                    raiseAllErrors=True,
                )

            logger.info("Checkpoint deleted successfully for the Input: [{}]".format(ckpt_key))
        except Exception:
            # Do nothing as the checkpoint doesn't exist
            return

    def handleRemove(self, conf_info):
        """
        Handle the delete operation.
        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        input_name = self.callerArgs.id
        session_key = self.getSessionKey()
        stanza = BATCH_STANZA_PREFIX.format(BATCH_STANZA_PATH.format(input_name))
        encoded_stanza = quote(stanza, safe="")

        rest.simpleRequest(
            "/servicesNS/nobody/{}/configs/conf-inputs/{}".format(APP_NAME, encoded_stanza),
            sessionKey=session_key,
            method="DELETE",
            getargs={"output_mode": "json"},
            raiseAllErrors=True,
        )

        self.check_point_delete_helper(input_name)

        reload_batch_input(self.getSessionKey())

        super(CustomConfigMigrationHandler, self).handleRemove(conf_info)

    def handleEdit(self, conf_info):
        """
        Handle the edit operation.
        :param conf_info: Default parameter generated by AOB which is used to pass the response
        """
        if 'disabled' not in self.payload.keys():
            session_key = self.getSessionKey()
            input_name = self.callerArgs.id
            stanza = BATCH_STANZA_PREFIX.format(BATCH_STANZA_PATH.format(input_name))
            encoded_stanza = quote(stanza, safe="")
            postargs = {
                "index": self.payload.get('index', "default")
            }
            rest.simpleRequest(
                "/servicesNS/nobody/{}/configs/conf-inputs/{}".format(APP_NAME, encoded_stanza),
                sessionKey=session_key,
                method="POST",
                getargs={"output_mode": "json"},
                postargs=postargs,
                raiseAllErrors=True,
            )

        reload_batch_input(self.getSessionKey())

        super(CustomConfigMigrationHandler, self).handleEdit(conf_info)


fields = [
    field.RestField(
        'interval',
        required=True,
        encrypted=False,
        default=None,
        validator=IntervalValidator()
    ), 
    field.RestField(
        'index',
        required=True,
        encrypted=False,
        default='default',
        validator=validator.String(
            min_len=1, 
            max_len=80, 
        )
    ), 
    field.RestField(
        'data_type',
        required=True,
        encrypted=False,
        default=None,
        validator=InputValidator()
    ), 
    field.RestField(
        'collect_data_for',
        required=True,
        encrypted=False,
        default='1',
        validator=None
    ), 
    field.RestField(
        'global_account',
        required=True,
        encrypted=False,
        default=None,
        validator=None
    ),

    field.RestField(
        'disabled',
        required=False,
        validator=None
    )

]
model = RestModel(fields, name=None)

endpoint = DataInputModel(
    'riskiq_security_intelligence_service',
    model,
)


if __name__ == '__main__':
    admin_external.handle(
        endpoint,
        handler=CustomConfigMigrationHandler,
    )
