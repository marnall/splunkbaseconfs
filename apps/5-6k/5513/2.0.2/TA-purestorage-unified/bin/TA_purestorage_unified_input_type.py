import ta_purestorage_unified_declare  # noqa:   401

import logging
import os

import splunk.admin as admin
from splunktaucclib.rest_handler.error import RestError
from solnlib import conf_manager
from logger_manager import setup_logging
from splunk_aoblib.rest_migration import ConfigMigrationHandler
import splunk.rest as rest
from purestorage_unified_utils import get_conf_file, input_with_account_exists

logger = setup_logging("purestorage_unified_ta_input_validation")
logger.setLevel(logging.INFO)
APP_NAME = __file__.split(os.sep)[-3]


class InputType(ConfigMigrationHandler):
    """REST Endpoint to populate the input type in the Inputs Configuration."""

    def get_account_type(self, session_key, account_name):
        """Method to retrieve account type from configuration."""
        try:
            cfm = conf_manager.ConfManager(
                session_key, APP_NAME, realm='__REST_CREDENTIAL__#{}#configs/conf-ta_purestorage_unified_account'
                .format(APP_NAME)
            )
            account_conf_file = cfm.get_conf('ta_purestorage_unified_account')
            account_type = account_conf_file.get(
                account_name).get('account_type')
            return account_type
        except Exception as exception:
            logger.error(
                "Failed to fetch system details from configuration, error={}".format(exception))
            return

    def check_type(self, session_key, input_type):
        """Method to check account type."""
        if input_type:
            account_name = self.callerArgs.data["global_account"][0]
            account_type = self.get_account_type(session_key, account_name)
            if not account_type:
                raise RestError(
                    409, "Failed to fetch system details from configuration.")
            if account_type == "flash_array_account" and input_type != "flasharray":
                logger.exception(
                    "Error: The System selected is for FlashArray. Please select the Input Type as FlashArray")
                raise RestError(
                    409,
                    "Error: The System selected is for FlashArray. Please select the Input Type as FlashArray")
            if account_type == "flash_blade_account" and input_type != "flashblade":
                logger.exception(
                    "Error: The System selected is for FlashBlade. Please select the Input Type as FlashBlade")
                raise RestError(
                    409,
                    "Error: The System selected is for FlashBlade. Please select the Input Type as FlashBlade")
            if account_type == "pure1" and input_type != "pure1":
                logger.exception(
                    "Error: The System selected is for Pure1. Please select the Input Type as Pure1")
                raise RestError(
                    409, "Error: The System selected is for Pure1. Please select the Input Type as Pure1")

    def handleEdit(self, conf_info):
        """
        Handles the edit operation.

        :param conf_info: Default parameter generated by AOB which is used to pass the response
        :return:
        """
        session_key = self.getSessionKey()
        input_type = self.payload.get("input_type")
        input_name = self.callerArgs.id
        global_account = self.payload.get("global_account")
        self.check_type(session_key, input_type)
        if input_type == "pure1":
            input_parser_obj, input_stanzas = get_conf_file("inputs.conf")
            existing_input = input_with_account_exists(input_stanzas, input_parser_obj, global_account,
                                                       input_type, None)
            if existing_input and existing_input != input_name:
                msg = 'Input {} is already configured with same pure1 credentials.'.format(existing_input)
                logger.error('Failed to create input. Reason: {}'.format(msg))
                raise RestError("409", msg)

        super(InputType, self).handleEdit(conf_info)

    def handleCreate(self, conf_info):
        """
        Handles the create operation.

        :param conf_info: Default parameter generated by AOB which is used to pass the response
        :return:
        """
        session_key = self.getSessionKey()
        input_type = self.payload.get("input_type")
        global_account = self.payload.get("global_account")
        self.check_type(session_key, input_type)
        if input_type == "pure1":
            input_parser_obj, input_stanzas = get_conf_file("inputs.conf")
            existing_input = input_with_account_exists(input_stanzas, input_parser_obj, global_account,
                                                       input_type, None)
            if existing_input:
                msg = 'Input {} is already configured with same pure1 credentials.'.format(existing_input)
                logger.error('Failed to create input. Reason: {}'.format(msg))
                raise RestError("409", msg)
        super(InputType, self).handleCreate(conf_info)

    def handleRemove(self, conf_info):
        """
        Handles the delete operation.

        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        session_key = self.getSessionKey()

        key = '{"_key":{"$regex":"^' + str(self.callerArgs.id) + '.*"}}'
        try:
            _, _ = rest.simpleRequest(
                "/servicesNS/nobody/" + str(ta_purestorage_unified_declare.ta_name) + "/storage/collections/data/"
                + str(ta_purestorage_unified_declare.ta_name.replace('-', '_')) + "_checkpointer",
                sessionKey=session_key, method='DELETE', getargs={"output_mode": "json", "query": key},
                raiseAllErrors=True)
        except Exception as e:
            logger.error(
                "PureStorage Error: An Error Occured while deleting checkpoint for input: {inp}. Error:{err}."
                " Removing input:{inp}".format(
                    inp=str(self.callerArgs.id), err=str(e)))

        super(InputType, self).handleRemove(conf_info)


if __name__ == "__main__":
    admin.init(InputType, admin.CONTEXT_APP_AND_USER)
