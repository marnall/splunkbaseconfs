def sanitize_app_name(title):
    name = title.strip().lower()
    name = re.sub(r'[^a-z0-9\s_-]', '', name)
    name = re.sub(r'[\s-]+', '_', name)
    name = re.sub(r'_+', '_', name)
    name = name.strip('_')
    return name or 'styled_app'


def xml_escape(text):
    """Escape special characters for XML content."""
    text = str(text)
    text = text.replace('&', '&amp;')
    text = text.replace('<', '&lt;')
    text = text.replace('>', '&gt;')
    text = text.replace('"', '&quot;')
    text = text.replace("'", '&apos;')
    return text


def write_file(base_dir, relative_path, content):
    path = os.path.join(base_dir, relative_path)
    parent = os.path.dirname(path)
    if not os.path.isdir(parent):
        os.makedirs(parent, exist_ok=True)
    with open(path, 'w') as f:
        f.write(content)


def save_image(data_url, base_dir, relative_path):
    """Decode a base64 data URL and write the image file."""
    try:
        if ',' in data_url:
            encoded = data_url.split(',', 1)[1]
        else:
            encoded = data_url
        image_bytes = base64.b64decode(encoded)
        path = os.path.join(base_dir, relative_path)
        parent = os.path.dirname(path)
        if not os.path.isdir(parent):
            os.makedirs(parent, exist_ok=True)
        with open(path, 'wb') as f:
            f.write(image_bytes)
    except Exception:
        pass  # Skip silently — image is optional


