def generate_nav_xml(primary_hex, dashboard_ids):
    xml = '<nav color="%s">\n' % primary_hex
    for i, dash_id in enumerate(dashboard_ids):
        if i == 0:
            xml += '  <view name="%s" default="true" />\n' % dash_id
        else:
            xml += '  <view name="%s" />\n' % dash_id
    xml += (
        '  <collection label="Splunk">\n'
        '    <view name="search" />\n'
        '    <view name="datasets" />\n'
        '    <view name="reports" />\n'
        '    <view name="alerts" />\n'
        '    <view name="dashboards" />\n'
        '  </collection>\n'
        '</nav>\n'
    )
    return xml


def generate_dashboard_css(styles, has_background, app_name):
    css = '/* Generated by DASH - Styled App Builder */\n\n'

    # CSS Variables block
    css += ':root {\n'
    var_map = {
        '--primary-color': 'primary_color',
        '--secondary-color': 'secondary_color',
        '--accent-color': 'accent_color',
        '--background-color': 'background_color',
        '--foreground-color': 'foreground_color',
        '--font-color': 'font_color',
        '--navbar-font-color': 'navbar_font_color',
        '--table-primary-color': 'table_primary_color',
        '--table-secondary-color': 'table_secondary_color',
        '--table-accent-color': 'table_accent_color',
        '--table-border-color': 'table_border_color',
        '--table-font-color': 'table_font_color',
        '--font-family': 'font_family',
        '--title-font-size': 'title_font_size',
        '--subtitle-font-size': 'subtitle_font_size',
        '--paragraph-font-size': 'paragraph_font_size',
    }
    for var_name, key in var_map.items():
        css += '  %s: %s;\n' % (var_name, styles.get(key, 'inherit'))
    css += '}\n\n'

    # App bar
    css += (
        'div[data-view="views/shared/appbar/Master"] {\n'
        '  background-color: var(--primary-color) !important;\n'
        '}\n\n'
    )

    # Dashboard title
    css += (
        'h1.dashboard-title.dashboard-header-title {\n'
        '  color: var(--secondary-color) !important;\n'
        '  font-size: var(--title-font-size) !important;\n'
        '}\n\n'
    )

    # Panel titles
    css += (
        'h2.panel-title {\n'
        '  color: var(--accent-color) !important;\n'
        '  background-color: var(--foreground-color) !important;\n'
        '  font-size: var(--subtitle-font-size) !important;\n'
        '}\n\n'
    )

    # Dashboard body background
    css += '.main-section-body.dashboard-body, body {\n'
    if has_background:
        css += (
            '  background: linear-gradient(var(--background-color), var(--background-color)),\n'
            '              url("/static/app/%s/background.png") !important;\n'
            '  background-size: cover !important;\n'
            '  background-position: center center !important;\n'
            '  background-attachment: fixed !important;\n'
        ) % app_name
    else:
        css += '  background-color: var(--background-color) !important;\n'
    css += '}\n\n'

    # Chart backgrounds transparent
    css += (
        '.dashboard-cell .highcharts-background,\n'
        '.dashboard-cell .highcharts-root,\n'
        'div.chart.shared-jschart > div {\n'
        '  background-color: transparent !important;\n'
        '  fill: transparent !important;\n'
        '}\n\n'
    )

    # Panel backgrounds (shared report visualizer + dashboard panels)
    css += (
        '.shared-reportvisualizer,\n'
        '.dashboard-row .dashboard-panel {\n'
        '  background-color: var(--foreground-color) !important;\n'
        '}\n\n'
    )

    # Font color
    css += (
        'text,\n'
        'p.description.dashboard-header-description {\n'
        '  fill: var(--font-color) !important;\n'
        '  color: var(--font-color) !important;\n'
        '}\n\n'
    )

    # Navbar font color
    css += (
        'a[data-view="views/shared/appbar/Button"] span {\n'
        '  color: var(--navbar-font-color) !important;\n'
        '}\n\n'
    )

    # Typography
    css += (
        '.main-section-body.dashboard-body {\n'
        '  font-size: var(--paragraph-font-size) !important;\n'
        '  font-family: var(--font-family) !important;\n'
        '}\n\n'
    )

    # Table header
    css += (
        '.table-chrome thead th {\n'
        '  background-color: var(--table-primary-color) !important;\n'
        '  color: var(--table-accent-color) !important;\n'
        '  border: 1px solid var(--table-border-color) !important;\n'
        '}\n\n'
    )

    # Table body
    css += (
        '.table-chrome tbody td {\n'
        '  background-color: var(--table-secondary-color) !important;\n'
        '  color: var(--table-font-color) !important;\n'
        '  border: 1px solid var(--table-border-color) !important;\n'
        '}\n\n'
    )

    # Panel menu visibility
    panel_visible = styles.get('panel_menu_visible', 'on')
    if panel_visible != 'on':
        css += (
            '/* Panel menus hidden */\n'
            '.view-mode .element-footer .menus '
            '{ display: none !important; }\n\n'
        )
    else:
        # Hide individual buttons as needed
        btn_map = {
            'panel_btn_search': 'a[aria-label="Open in Search"]',
            'panel_btn_export': 'a[aria-label="Export"]',
            'panel_btn_inspect': 'a[aria-label="Inspect"]',
            'panel_btn_refresh': 'a[aria-label="Refresh"]',
        }
        hidden_rules = []
        for key, selector in btn_map.items():
            if styles.get(key, 'on') != 'on':
                hidden_rules.append(
                    '%s { display: none !important; }' % selector
                )
        if hidden_rules:
            css += '/* Individual panel buttons hidden */\n'
            css += '\n'.join(hidden_rules) + '\n\n'

    return css


def generate_dashboard_xml(dashboard_def, graph_colors, styles=None, cim_panels=None):
    """Generate Simple XML for a single dashboard from a use case definition."""
    if graph_colors == 'colorized':
        series_colors = '[0xFFC8DD,0xFFDFD3,0xFDE4CF,0xD8E2DC,0xA2D2FF,0xBDE0FE]'
    else:
        series_colors = '[0xDCDCDC,0xD3D3D3,0xC0C0C0,0xA9A9A9,0x808080,0x696969]'

    if styles is None:
        styles = {}

    # Build dynamic root attributes
    attrs = 'version="1.1" theme="light"'

    # Stylesheet always included
    attrs += ' stylesheet="dashboard.css"'

    # Always hide the Splunk bar in generated dashboards
    attrs += ' hideSplunkBar="true"'

    # Boolean dashboard attributes
    bool_attrs = [
        ('attr_hideChrome', 'hideChrome'),
        ('attr_hideAppBar', 'hideAppBar'),
        ('attr_hideEdit', 'hideEdit'),
        ('attr_hideExport', 'hideExport'),
        ('attr_hideTitle', 'hideTitle'),
        ('attr_hideFilters', 'hideFilters'),
        ('attr_onunloadCancelJobs', 'onunloadCancelJobs'),
    ]
    for style_key, xml_attr in bool_attrs:
        if styles.get(style_key) == 'on':
            attrs += ' %s="true"' % xml_attr

    # Refresh interval
    refresh_val = styles.get('attr_refresh', '')
    if refresh_val and str(refresh_val).strip() and str(refresh_val).strip() != '0':
        attrs += ' refresh="%s"' % xml_escape(str(refresh_val).strip())

    # Use <form> for time picker support
    xml = '<form %s>\n' % attrs
    xml += '  <label>%s</label>\n' % xml_escape(dashboard_def['title'])
    xml += '  <description>%s</description>\n' % xml_escape(
        dashboard_def.get('description', ''))
    xml += '\n'

    # Time picker fieldset
    xml += (
        '  <fieldset submitButton="false" autoRun="true">\n'
        '    <input type="time" token="time" searchWhenChanged="true">\n'
        '      <label>Time Range</label>\n'
        '      <default>\n'
        '        <earliest>-24h@h</earliest>\n'
        '        <latest>now</latest>\n'
        '      </default>\n'
        '    </input>\n'
        '  </fieldset>\n\n'
    )

    # Build a flat list of panels for CIM index matching
    panel_index = 0

    for row in dashboard_def['rows']:
        xml += '  <row>\n'
        for panel in row['panels']:
            xml += '    <panel>\n'
            xml += '      <title>%s</title>\n' % xml_escape(panel['title'])

            panel_type = panel['type']

            # Use CIM query if available at this panel index
            if cim_panels and panel_index < len(cim_panels):
                search_query = cim_panels[panel_index].get(
                    'query', panel['search'])
            else:
                search_query = panel['search']

            if panel_type == 'table':
                xml += '      <table>\n'
                xml += '        <search>\n'
                xml += '          <query>%s</query>\n' % xml_escape(search_query)
                xml += '          <earliest>$time.earliest$</earliest>\n'
                xml += '          <latest>$time.latest$</latest>\n'
                xml += '        </search>\n'
                xml += '        <option name="drilldown">none</option>\n'
                xml += '      </table>\n'
            elif panel_type == 'single':
                xml += '      <single>\n'
                xml += '        <search>\n'
                xml += '          <query>%s</query>\n' % xml_escape(search_query)
                xml += '          <earliest>$time.earliest$</earliest>\n'
                xml += '          <latest>$time.latest$</latest>\n'
                xml += '        </search>\n'
                xml += '        <option name="drilldown">none</option>\n'
                xml += '        <option name="height">120</option>\n'
                xml += '      </single>\n'
            else:
                # chart type
                chart_type = panel.get('chart_type', 'column')
                xml += '      <chart>\n'
                xml += '        <search>\n'
                xml += '          <query>%s</query>\n' % xml_escape(search_query)
                xml += '          <earliest>$time.earliest$</earliest>\n'
                xml += '          <latest>$time.latest$</latest>\n'
                xml += '        </search>\n'
                xml += '        <option name="charting.chart">%s</option>\n' % chart_type
                xml += '        <option name="charting.drilldown">none</option>\n'
                xml += '        <option name="charting.legend.placement">right</option>\n'
                xml += '        <option name="charting.lineWidth">2</option>\n'
                xml += '        <option name="charting.seriesColors">%s</option>\n' % series_colors
                xml += '      </chart>\n'

            xml += '    </panel>\n'
            panel_index += 1
        xml += '  </row>\n'
        xml += '\n'

    xml += '</form>\n'
    return xml


def create_app_structure(app_dir, app_title, app_name,
                         styles, logo_data, logo_36, logo_72,
                         background_data, graph_colors, use_case,
                         selected_dashboards=None,
                         query_type='sample', cim_queries=None):
    """Create a complete Splunk app directory structure with styled dashboards.

    Args:
        selected_dashboards: Optional list of dashboard IDs to include.
                             If None or empty, all dashboards are included.
        query_type: 'sample' for makeresults or 'cim' for tstats queries.
        cim_queries: Dict of CIM query definitions keyed by use case,
                     containing per-dashboard panel queries.
    """
    use_cases = get_use_cases()

    # Create directories
    dirs = [
        os.path.join(app_dir, 'default', 'data', 'ui', 'nav'),
        os.path.join(app_dir, 'default', 'data', 'ui', 'views'),
        os.path.join(app_dir, 'metadata'),
        os.path.join(app_dir, 'appserver', 'static'),
        os.path.join(app_dir, 'static'),
    ]
    for d in dirs:
        os.makedirs(d, exist_ok=True)

    # Write config files
    write_file(app_dir, 'default/app.conf',
               generate_app_conf(app_title, app_name))
    write_file(app_dir, 'metadata/default.meta',
               generate_default_meta())

    # Extract hex color for nav from primary_color form value
    primary_hex = styles.get('primary_color_hex', '#3c444d')

    # Get dashboards for the selected use case
    use_case_data = use_cases.get(use_case, use_cases['cybersecurity'])
    dashboards = use_case_data['dashboards']

    # Filter to selected dashboards if specified
    if selected_dashboards:
        dashboards = [d for d in dashboards if d['id'] in selected_dashboards]
        if not dashboards:
            # Fallback to all if selection produced nothing
            dashboards = use_case_data['dashboards']

    dashboard_ids = [d['id'] for d in dashboards]

    write_file(app_dir, 'default/data/ui/nav/default.xml',
               generate_nav_xml(primary_hex, dashboard_ids))

    # Generate and write dashboard.css
    has_bg = background_data is not None
    write_file(app_dir, 'appserver/static/dashboard.css',
               generate_dashboard_css(styles, has_bg, app_name))

    # Resolve CIM panels per dashboard by positional index
    # cim_queries is a list of dashboard objects, each with a 'panels' list
    cim_dashboard_list = None
    if query_type == 'cim' and cim_queries:
        if isinstance(cim_queries, list):
            cim_dashboard_list = cim_queries
        elif isinstance(cim_queries, dict) and 'dashboards' in cim_queries:
            cim_dashboard_list = cim_queries['dashboards']

    # Generate a dashboard XML file for each dashboard in the use case
    all_dashboards = use_case_data['dashboards']
    for dashboard_def in dashboards:
        # Find the positional index in the full dashboard list
        cim_panels = None
        if cim_dashboard_list:
            try:
                dash_idx = all_dashboards.index(dashboard_def)
                if dash_idx < len(cim_dashboard_list):
                    cim_entry = cim_dashboard_list[dash_idx]
                    if isinstance(cim_entry, dict):
                        cim_panels = cim_entry.get('panels', [])
                    elif isinstance(cim_entry, list):
                        cim_panels = cim_entry
            except (ValueError, IndexError):
                pass

        xml = generate_dashboard_xml(
            dashboard_def, graph_colors,
            styles=styles, cim_panels=cim_panels
        )
        write_file(app_dir,
                   'default/data/ui/views/%s.xml' % dashboard_def['id'],
                   xml)

    # Save images — use pre-resized variants from the frontend
    if logo_data:
        save_image(logo_data, app_dir, 'appserver/static/logo.png')

        icon_36 = logo_36 or logo_data
        icon_72 = logo_72 or logo_data
        save_image(icon_36, app_dir, 'static/appIcon.png')
        save_image(icon_72, app_dir, 'static/appIcon_2x.png')
        save_image(icon_36, app_dir, 'static/appIconAlt.png')
        save_image(icon_36, app_dir, 'static/appLogo.png')
        save_image(icon_72, app_dir, 'static/appLogo_2x.png')

    if background_data:
        save_image(background_data, app_dir, 'appserver/static/background.png')
