"""
This module is used to filter and reload PATH.
This file is generated by Splunk add-on builder
"""

import hashlib
import os
import shutil
import sys
import re

py_version = "aob_py3"

ta_name = "phantom"
ta_lib_name = "ta_addonphantom"
pattern = re.compile(r"[\\/]etc[\\/]apps[\\/][^\\/]+[\\/]bin[\\/]?$")
new_paths = [path for path in sys.path if not pattern.search(path) or ta_name in path]

# Remove any files/dirs under bin/ that are not listed in bin.manifest.
# bin.manifest is generated at build time and contains the canonical set of
# paths for this release. Anything not on the list is a stale artifact from a
# previous install (e.g. packages that moved location between releases).
#
# A stamp file (bin.manifest.done) caches the manifest checksum so the
# cleanup only runs once per release rather than on every import.
_bin_dir = os.path.dirname(__file__)
_manifest_path = os.path.join(_bin_dir, 'bin.manifest')
_stamp_path = os.path.join(_bin_dir, 'bin.manifest.done')

if os.path.isfile(_manifest_path):
    _manifest_data = open(_manifest_path).read()
    _manifest_hash = hashlib.md5(_manifest_data.encode()).hexdigest()

    _already_clean = False
    try:
        _already_clean = open(_stamp_path).read().strip() == _manifest_hash
    except OSError:
        pass

    if not _already_clean:
        _manifest = set(_manifest_data.splitlines())
        for _entry in os.listdir(_bin_dir):
            if _entry == '__pycache__':
                continue
            _entry_path = os.path.join(_bin_dir, _entry)
            if os.path.isdir(_entry_path):
                _has_entry = any(p.startswith(_entry + os.sep) or p.startswith(_entry + '/') for p in _manifest)
                if not _has_entry:
                    shutil.rmtree(_entry_path, ignore_errors=True)
            elif _entry not in _manifest:
                try:
                    os.remove(_entry_path)
                except OSError:
                    pass
        try:
            open(_stamp_path, 'w').write(_manifest_hash)
        except OSError:
            pass

bin_dir = os.path.dirname(__file__)
aob_py3_dir = os.path.sep.join([bin_dir, ta_lib_name, py_version])
ta_lib_dir = os.path.sep.join([bin_dir, ta_lib_name])

# Remove bin_dir from wherever it currently sits so we can re-insert it after
# aob_py3. This prevents stale packages left in bin/ from a previous install
# (e.g. six.py 1.13.0) from shadowing the vendored versions in aob_py3.
new_paths = [p for p in new_paths if os.path.normpath(p) != os.path.normpath(bin_dir)]
new_paths.insert(0, bin_dir)      # bin/ last among our paths — entry points only
new_paths.insert(0, ta_lib_dir)
new_paths.insert(0, aob_py3_dir)  # aob_py3 first — wins for all vendored packages
sys.path = new_paths
