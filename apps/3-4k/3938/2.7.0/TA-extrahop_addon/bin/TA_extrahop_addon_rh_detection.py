class CustomInputHandler(ConfigMigrationHandler):
    """
    This class handles the parameters in the input page
    """
    def __init__(self, *args, **kwargs):
        ConfigMigrationHandler.__init__(self, *args, **kwargs)

    def get_account_name(self, session_key, input_value):
        input_name = "detection://" + str(input_value)
        try:
            _ , response_content = rest.simpleRequest(
                "/servicesNS/nobody/" + str(ta_name) + "/configs/conf-inputs/" + quote_plus(input_name),
                sessionKey=session_key,
                getargs={"output_mode": "json"},
                raiseAllErrors=True,
            )
            res = json.loads(response_content)
            if res.get("entry"):
                for inputs in res.get("entry"):
                    if(inputs.get("name") == input_name and (inputs['content'].get('global_account'))):
                        account_name = inputs['content']['global_account']
                        return account_name
        except Exception as e:
            pass
        
        return None

    def handleRemove(self, conf_info):
        """ 
        Handles the delete operation
        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        session_key = self.getSessionKey()
        input_name = self.callerArgs.id
        account_name = self.get_account_name(session_key, input_name)
        if account_name:
            checkpoint_name = "_".join([input_name, account_name, "post_v912", "detections"])
            try:
                _, _ = rest.simpleRequest(
                    "/servicesNS/nobody/" + str(ta_name) +"/storage/collections/data/" + str(ta_name.replace('-','_')) + "_checkpointer/" + str(checkpoint_name),
                    sessionKey=session_key, method='DELETE', getargs={"output_mode": "json"}, raiseAllErrors=True)
            except Exception as e:
                pass
        super(CustomInputHandler, self).handleRemove(conf_info)


if __name__ == '__main__':
    admin_external.handle(
        endpoint,
        handler=CustomInputHandler,
    )
