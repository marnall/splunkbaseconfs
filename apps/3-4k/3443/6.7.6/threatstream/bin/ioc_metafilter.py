# pylint: disable=wrong-import-position, import-error, no-self-use
"""
Custom Splunk command that allows us to filter the iocmeta from events as generated by ts_match.py
"""
import sys
import json
import re
import time
import util.utils
import six

from splunklib.searchcommands import dispatch, StreamingCommand, Configuration, Option, validators


@Configuration()
class IocMetaMatch(StreamingCommand):
    """
    This Splunk command is designed to take the IOC match metadata and make a specific one win out
    allowing customers to give priority to specific organisations, min/max confidences and
    """

    # KVStore constant parameters
    tip_field_name = "tipreport"
    act_field_name = "actor"
    org_field_name = "org"
    conf_field_name = "confidence"
    sev_field_name = "severity"
    tag_field = "detail"
    feeds_field = "source"
    feeds_id_field = "source_feed_id"
    itype_field = "itype"

    # Parameters for constants within the class
    time_created = "date_first"
    time_modified = "date_last"

    sev_low = "low"
    sev_medium = "medium"
    sev_high = "high"
    sev_critical = "very-high"

    pri_opt_none = "disabled"
    pri_opt_soft = "soft"
    pri_opt_hard = "hard"

    time_priority_valid_values = [time_created, time_modified]

    severity_options = {
        sev_low: 0,
        sev_medium: 1,
        sev_high: 2,
        sev_critical: 3,
    }

    inverse_severity_options = {
        0: sev_low,
        1: sev_medium,
        2: sev_high,
        3: sev_critical
    }

    field_map = {
        "event.sourcetype": "orig_sourcetype",
        "event.source": "orig_source",
        "event.host": "orig_host",
        "event.et": "event_time",
        "event.ts_lookup_key_value": "indicator",
        "event._time": "event_time",
    }

    priority_options = [pri_opt_none, pri_opt_soft, pri_opt_hard]

    # Splunk command options
    min_confidence = Option(default=0, validate=validators.Integer())
    max_confidence = Option(default=100, validate=validators.Integer())
    min_severity = Option(default=sev_low)
    max_severity = Option(default=sev_critical)
    org_name = Option(default=None)
    org_match = Option(default=pri_opt_soft)
    time_priority = Option(default=time_modified)
    severity_match = Option(default=pri_opt_hard)
    has_actor = Option(default=pri_opt_none)
    has_tipreport = Option(default=pri_opt_none)
    tag = Option(default=None)
    tag_match = Option(default=pri_opt_hard)
    feed = Option(default=None)
    feed_match = Option(default=pri_opt_soft)
    feed_id = Option(default=None)
    feed_id_match = Option(default=pri_opt_soft)
    field_prefix = Option(default=None)  # string field e.g. ts.
    itype = Option(default=None)  # regex field e.g. (apt|malware)
    ae_sourcetype = Option(default="anomali:enterprise:iocmatch")
    legacy_data_sourcetype = Option(default="stash")

    metrics = {
        "checks": 0,
        "json_decode": 0,
        "overall": 0
    }

    itype_regex = ""
    tag_regex = ""

    def validate(self):
        """
        This method is used to provide better validation of the command parameters
        """
        err_msg = None
        if self.has_actor not in self.priority_options:
            err_msg = "Invalid option specified in has_actor command switch, expecting disabled, soft or hard"
        elif self.has_tipreport not in self.priority_options:
            err_msg = "Invalid option specified in has_tipreport command switch, expecting disabled, soft or hard"
        elif self.org_match not in self.priority_options:
            err_msg = "Invalid option specified in org_match command switch, expecting disabled, soft or hard"
        elif self.tag_match not in self.priority_options:
            err_msg = "Invalid option specified in tag_match command switch, expecting disabled, soft or hard"
        elif self.severity_match not in self.priority_options:
            err_msg = "Invalid option specified in severity_match command switch, expecting disabled, soft or hard"
        elif self.feed_match not in self.priority_options:
            err_msg = "Invalid option specified in feed_match command switch, expecting disabled, soft or hard"
        elif self.time_priority not in self.time_priority_valid_values:
            err_msg = "Invalid time option specified, expecting date_first or date_last"
        elif self.feed_id_match not in self.priority_options:
            err_msg = "Invalid option specified in feed_id_match command switch, expecting disabled, soft or hard"
        elif self.max_severity not in self.severity_options or \
                self.min_severity not in self.severity_options:
            err_msg = "Invalid option specified in max/min severity, expecting low, medium, high or very-high"
        elif not (0 <= self.max_confidence <= 100 and 0 <= self.min_confidence <= 100):
            err_msg = "Invalid option specified for min/max confidence, expecting value between 0 and 100"

        self.logger.warning(err_msg)
        if err_msg is not None:
            self.raiseexception(err_msg)

    def stream(self, records):
        """
        Override of the splunklib search command stream method. This function will loop through
        splunk records and transform events to have singular

        Args:
            records (dict): the splunk log to transform

        Yields:
            record (dict): the transformed splunk log
        """
        self.logging_level = "INFO"
        self.validate()
        metric_overall_t0 = time.time()

        self.itype_regex = re.compile(self.itype) if self.itype is not ("" or None) else None
        self.tag_regex = re.compile(self.tag) if self.tag is not ("" or None) else None

        sourcetypes = {
            self.ae_sourcetype: 0,
            self.legacy_data_sourcetype: 0,
            "anomali_matches": 0
        }
        if not self.fieldnames:
            self.fieldnames.append("anomali_results")

        for record in records:
            # Munge the previous data formats and come up with
            self.logger.info("Sourcetype = %s" % record["_sourcetype"])
            if record["_sourcetype"] == self.ae_sourcetype or record["_sourcetype"] == self.legacy_data_sourcetype:
                sourcetypes[record["_sourcetype"]] = sourcetypes[record["_sourcetype"]] +1
                self.logger.info("%s" % record)
                new_record, is_transformed, ts_fieldnames = self.ae_data_munge(record)

                # Conduct the checks and tests to determine that the AE/legacy match will abide by parameters

                munged_record = self.metacheck([new_record])

                if munged_record:
                    for key in ts_fieldnames:
                        if self.field_prefix is not None:
                            munged_record["%s%s" % (self.field_prefix, key)] = munged_record[key]
                            munged_record.pop(key)
                        else:
                            record[key] = munged_record[key]
                    yield munged_record

            else:
                sourcetypes["anomali_matches"] = sourcetypes["anomali_matches"] + 1
                for field in self.fieldnames:
                    if not record.get(field, False):
                        continue

                    metric_decode_t0 = time.time()
                    try:
                        ioc_match_results = json.loads(record.get(field))
                    except ValueError:
                        continue

                    self.metrics["json_decode"] += time.time() - metric_decode_t0

                    for indicator in ioc_match_results:
                        indicator_match_results = ioc_match_results[indicator]

                        metric_check_t0 = time.time()
                        # Logic here is to determine metadata entries that don't match
                        # filter out non-applicable confidence matches

                        ioc_meta_dict = self.metacheck(indicator_match_results)

                        self.metrics["checks"] += time.time() - metric_check_t0

                        if not ioc_meta_dict:
                            continue

                        ioc_meta_dict.pop("_time", "")  # Prevent reordering of events within the Splunk pipeline

                        for key in ioc_meta_dict:
                            if self.field_prefix is not None:
                                record["%s%s" % (self.field_prefix, key)] = ioc_meta_dict[key]
                            else:
                                record[key] = ioc_meta_dict[key]

                        # Remove the field containing the anomali_results produced by iocmatchbatch SPL command
                        record.pop(field, "")
                        yield record

        self.metrics["overall"] += time.time() - metric_overall_t0
        self.logger.info("Metric: overall:%s,checks:%s,json_decode:%s" % (
            self.metrics["overall"],
            self.metrics["checks"],
            self.metrics["json_decode"],
        ))

        self.logger.info("Examined sourcetyped logs %s" % sourcetypes)

    def metacheck(self, iocmeta_set):
        """
        Performs the
        Args:
            iocmeta_set (list): A list of dicts for the entries within the iocmetafilter set

        Returns:
            final_set (list): The final selected IOC metadata dict
        """

        indicator_match_results = self.iocmeta_bounds_filter(
            iocmeta_set,
            self.conf_field_name,
            self.min_confidence,
            self.max_confidence,
        )

        # Org Name filter
        if self.org_name is not None:
            indicator_match_results = self.iocmeta_exact_filter(
                indicator_match_results,
                self.org_field_name,
                self.org_name,
                self.org_match
            )

        # Actor Filter
        if self.has_actor != self.pri_opt_none:
            indicator_match_results = self.iocmeta_exists_filter(
                indicator_match_results,
                self.act_field_name,
                self.has_actor
            )

        # Threat Bulletin Filter
        if self.has_tipreport != self.pri_opt_none:
            indicator_match_results = self.iocmeta_exists_filter(
                indicator_match_results,
                self.tip_field_name,
                self.has_tipreport
            )

        # Tag filter - we check that the IOC has both the tag field
        # and that it has the correct tag regex match
        if self.tag_regex is not None:
            indicator_match_results = self.iocmeta_exists_filter(
                indicator_match_results,
                self.tag_field,
                self.tag_match
            )

            indicator_match_results = self.iocmeta_regex_filter(
                indicator_match_results,
                self.tag_regex,
                self.tag_field,
                self.tag_match
            )

        # Feed Name Filter
        if self.feed is not None:
            indicator_match_results = self.iocmeta_exact_filter(
                indicator_match_results,
                self.feeds_field,
                self.feed,
                self.feed_match
            )

        # Feed ID Filter
        if self.feed_id is not None:
            indicator_match_results = self.iocmeta_exact_filter(
                indicator_match_results,
                self.feeds_id_field,
                self.feed_id,
                self.feed_id_match
            )

        # Severity Filter
        if self.min_severity != self.sev_low or self.max_severity != self.sev_critical:
            indicator_match_results = self.filter_sev(indicator_match_results)

        # itype Filter
        if self.itype_regex is not None:
            indicator_match_results = self.iocmeta_regex_filter(
                indicator_match_results,
                self.itype_regex,
                self.itype_field,
                self.pri_opt_hard
            )

        # Logic here is to determine metadata win out clause
        ioc_meta_dict = self.filter_to_latest(indicator_match_results)

        return ioc_meta_dict

    def ae_data_munge(self, splunk_record):
        """
        Performs a data manipulation on the record to allow AE and Splunk Legacy match data to current standards
        Args:
            splunk_record: the splunk event record to transform

        Returns:
            transformed_record (dict), is_transformed (bool)
        """
        field_counter = 0
        is_transformed = False

        event_record = []
        event_ts_record = []
        ts_record = []

        ts_fieldname = set()
        for record_field in splunk_record:
            if "event.ts_" in record_field:
                event_ts_record.append(record_field)
            elif "event." in record_field:
                event_record.append(record_field)
            elif "ts_" in record_field:
                ts_record.append(record_field)

        # Strip event. and event.ts_* fields and replace with *
        for record_field in splunk_record:
            # Take the first value for a semi-colon'd field that is not detail (tags)
            if ";" in splunk_record[record_field] and record_field != self.tag_field and record_field!="_raw":
                splunk_record.update({record_field: splunk_record[record_field].split(";")[0]})

        for record_field in event_ts_record:
            self.logger.info("Transformed field %s to %s" % (record_field, record_field[9:]))
            field_counter += 1
            new_fieldname = record_field[9:]
            splunk_record.update({new_fieldname: splunk_record[record_field]})
            ts_fieldname.add(new_fieldname)
            splunk_record.pop(record_field)

        for record_field in ts_record:
            self.logger.info("Transformed field %s to %s" % (record_field, record_field[9:]))
            field_counter += 1
            new_fieldname = record_field[3:]
            splunk_record.update({new_fieldname: splunk_record[record_field]})
            ts_fieldname.add(new_fieldname)
            splunk_record.pop(record_field)

        for record_field in event_record:
            field_counter += 1
            splunk_record.update({record_field[6:]: splunk_record[record_field]})

        # Ensure the correct types are applied to float fields
        if self.conf_field_name in splunk_record:
            if splunk_record[self.conf_field_name] == "":
                splunk_record.update({self.conf_field_name: 0})
            else:
                self.logger.warning("Confidence field name %s has value %s and type %s" % ( self.conf_field_name, splunk_record[self.conf_field_name], type(splunk_record[self.conf_field_name]) ))
                splunk_record.update({self.conf_field_name: float(splunk_record.get(self.conf_field_name, 0))})
        if self.feeds_id_field in splunk_record:
            splunk_record.update({self.feeds_id_field_field_name:float(splunk_record.get(self.feeds_id_field, 0))})

        if field_counter > 0:
            is_transformed = True

        return splunk_record, is_transformed, ts_fieldname

    def iocmeta_exists_filter(self, array_to_filter, field_to_filter, match_type):
        """
        Filters out any dicts in the array_to_filter that have the specified field as "" or None

        Args:
            array_to_filter (list): a list of dicts to filter
            field_to_filter (str): which field to check
            match_type (str): whether the

        Returns:
            filtered_list (list): the filtered list
        """
        filtered_list = [x for x in array_to_filter if x.get(field_to_filter, False) and x.get(field_to_filter, "") != ""]

        return self.hard_or_soft_match(array_to_filter, filtered_list, match_type)

    def iocmeta_exact_filter(self, array_to_filter, field_to_filter, field_value, match_type):
        """
        Filters out any dicts in the array_to_filter that do not have an exact match
        Args:
            array_to_filter (list): a list of dicts to filter
            field_to_filter (str): the field to filter
            field_value (str): the exact value the field must be
            match_type (str): specify a hard or soft match

        Returns:
            filtered_list (dict): the metadata items that pass the criteria
        """

        filtered_list = [x for x in array_to_filter if x.get(field_to_filter, "") == field_value]

        return self.hard_or_soft_match(array_to_filter, filtered_list, match_type)

    def iocmeta_bounds_filter(self, array_to_filter, field_to_filter, min_value, max_value):
        """
        Filters out any dict objects that have a field value outside the min and max
        field value from a list

        Args:
            array_to_filter (list): a list of dicts to filter
            field_to_filter (str): the field to filter
            min_value (int): the minimum the field must be to not be filtered
            max_value (int): the maximum the field must be to not be filtered

        Returns:
            filtered_list (list): A list of metadata objects that pass the bounds filter
        """

        filtered_list = []

        for metaitem in array_to_filter:
            if min_value <= metaitem.get(field_to_filter, 0) <= max_value:
                filtered_list.append(metaitem)

        return filtered_list

    def iocmeta_regex_filter(self, array_to_filter, compiled_regex, field_to_filter, match_type):
        """
        Filters out a field based on a regex match

        Args:
            array_to_filter (list): a list of dicts to filter
            compiled_regex (obj): compiled regex
            field_to_filter (str): the field to filter
            match_type (str): specify a hard or soft match

        Returns:
            filtered_list (list): A list of metadata objects that passes this filter
        """

        filtered_list = [x for x in array_to_filter if compiled_regex.search(x.get(field_to_filter, "")) is not None]

        return self.hard_or_soft_match(array_to_filter, filtered_list, match_type)

    def hard_or_soft_match(self, start_array, result_array, match_type):
        """
        Function to implement hard and soft matching. If matching is a hard match, return no results if all
        iocmetadata pieces fail a check. If matching is a soft match, return the starting results if all iocmetadata
        pieces fail a check
        Args:
            start_array (list):
            result_array (list):
            match_type (str):

        Returns:
            (list): the appriopriately matched results array
        """
        if match_type == self.pri_opt_soft:
            if result_array:
                return result_array
            else:
                return start_array
        elif match_type == self.pri_opt_hard:
            return result_array
        elif match_type == self.pri_opt_none:
            return start_array

    def filter_to_latest(self, ioc_match_results):
        """
        This function prioritises a single piece of metadata after filtering has taken place

        Args:
            ioc_match_results (list): a list of ioc metadata items that needs to be prioritised

        Returns:
            iocmeta_latest (dict): the metadata with the highest priority
        """
        ioc_base_timestamp = util.utils._parse_time("1990-01-01T00:00:00", self.logger)
        iocmeta_latest = dict()

        for metadata_entry in ioc_match_results:
            iocmeta_time = util.utils._parse_time(metadata_entry.get(self.time_priority), self.logger)
            if iocmeta_time > ioc_base_timestamp:
                iocmeta_latest = metadata_entry
                ioc_base_timestamp = iocmeta_time

        return iocmeta_latest

    def raiseexception(self, error_message):
        """
        A function that raises errors for this Splunk Command

        Args:
            error_message (str): the error message to log
        """
        self.logger.exception(error_message)
        self.write_error(error_message)
        sys.exit(error_message)

    def cast_sev(self, severity=None):
        """
        Return the opposite type of severity based on the severity list in this class

        Args:
            severity (int|str): the severity to convert

        Returns:
            (int|str): the converted severity
        """
        if isinstance(severity, int):
            return self.inverse_severity_options[severity]
        elif isinstance(severity, six.string_types):
            return self.severity_options[severity]
        else:
            raise TypeError("Expecting type str or int input, got %s" % type(severity))

    def filter_sev(self, array_to_filter):
        """
        Returns the array filtered so that the severities match those specified in the splunk
        command arguments

        Args:
            array_to_filter (list): the list of iocmeta dicts to filter

        Returns:
            (list): filtered list
        """

        sev_min = self.cast_sev(severity=self.min_severity)
        sev_max = self.cast_sev(severity=self.max_severity)
        array_to_return = [x for x in array_to_filter if sev_min <= self.cast_sev(severity=x.get(self.sev_field_name, "low")) <= sev_max]

        return self.hard_or_soft_match(array_to_filter, array_to_return, self.severity_match)


dispatch(IocMetaMatch, sys.argv, module_name=__name__)
