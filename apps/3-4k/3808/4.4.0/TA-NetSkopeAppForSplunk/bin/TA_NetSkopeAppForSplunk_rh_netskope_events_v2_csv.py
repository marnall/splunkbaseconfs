batch_stanza_v2 = path.join("$SPLUNK_HOME", "var", "spool", "splunk", "events", "{}", "{}*.csv")


class CustomConfigMigrationHandler(ConfigMigrationHandler):
    """
    Manage the Rest Handler for server
    :param ConfigMigrationHandler: inhereting ConfigMigrationHandler
    """
    
    def create_batch_stanza(self):
        event_types = ''.join(self.callerArgs.data["event_type"]).split("~")
        if event_types is not None:
            for event_type in event_types:
                event_type = "page" if event_type == "connection" else event_type
                stanza_name = self.get_batch_stanza_name(event_type)
                batch_input_stanza = deepcopy(NETSKOPE_INPUTS["netskope_events_v2_csv"])
                batch_input_stanza.update(
                    {
                        "name": "batch://{}".format(batch_stanza_v2.format(
                            event_type,
                            stanza_name
                        )),
                        "move_policy": "sinkhole",
                        "crcSalt": "<SOURCE>",
                        "disabled": "false",
                        "index": self.payload.get("index", "default"),
                        "sourcetype": "netskope:{}".format("connection" if event_type=="page" else event_type),
                    }
                )
                res, _ = rest.simpleRequest(
                    "/servicesNS/nobody/{}/configs/conf-inputs".format(APP_NAME),
                    self.getSessionKey(),
                    postargs=batch_input_stanza,
                    method="POST",
                    rawResult=True,
                    raiseAllErrors=True,
                )
                if res.status not in [201, 409]:
                    raise Exception("Unable to create batch input for Events (Iterator).")

    def get_batch_stanza_name(self,event_type):
        input_name = self.callerArgs.id
        acc_name = self.callerArgs.data["global_account"][0]
        stanza_name = "{}_{}_events_iterator_{}".format(acc_name, input_name, event_type)
        return stanza_name

    def handleCreate(self, confInfo):
        """Handle creation of account in config file."""
        self.create_batch_stanza()
        reload_batch_input(self.getSessionKey())

        super(CustomConfigMigrationHandler, self).handleCreate(confInfo)

    def handleRemove(self, conf_info):
        """
        Handles the delete operation
        :param confInfo: Default parameter generated by AOB which is used to pass the response
        """
        # Read global account used in the input
        input_name = self.callerArgs.id
        configs = cli.getConfStanza("inputs", "netskope_events_v2_csv://{}".format(input_name))
        acc_name = configs.get('global_account')
        event_types = configs.get('event_type').split("~")
        for event_type in event_types:
            event_type = "page" if event_type == "connection" else event_type
            stanza_name = "{}_{}_events_iterator_{}".format(acc_name, input_name, event_type)
            stanza = "batch://{}".format(batch_stanza_v2.format(event_type, stanza_name))
            session_key = self.getSessionKey()
            encoded_stanza = quote(stanza, safe="")
            rest.simpleRequest(
                "/servicesNS/nobody/{}/configs/conf-inputs/{}".format(APP_NAME, encoded_stanza),
                sessionKey=session_key,
                method="DELETE",
                getargs={"output_mode": "json"},
                raiseAllErrors=True,
            )

            reload_batch_input(self.getSessionKey())

            filenames = next(os.walk(LOCAL_STORAGE_PATH), (None, None, []))[2]
            for each in filenames:
                if each.startswith(stanza_name):
                    os.remove(os.path.join(LOCAL_STORAGE_PATH, each))

        super(CustomConfigMigrationHandler, self).handleRemove(conf_info)

    def handleEdit(self, conf_info):
        """
        Handles the edit operation
        :param conf_info: Default parameter generated by AOB which is used to pass the response
        """
        if 'disabled' not in self.payload.keys():  # While not enabling/disabling inputs
            event_types = ''.join(self.callerArgs.data["event_type"]).split("~")
            for event_type in event_types:
                event_type = "page" if event_type == "connection" else event_type
                session_key = self.getSessionKey()
                stanza_name = self.get_batch_stanza_name(event_type=event_type)
                stanza = "batch://{}".format(batch_stanza_v2.format(event_type, stanza_name))
                encoded_stanza = quote(stanza, safe="")
                postargs = {
                    "index": self.payload.get("index", "default"),
                }
                try:
                    rest.simpleRequest(
                        "/servicesNS/nobody/{}/configs/conf-inputs/{}".format(APP_NAME, encoded_stanza),
                        sessionKey=session_key,
                        method="POST",
                        getargs={"output_mode": "json"},
                        postargs=postargs,
                        raiseAllErrors=True,
                    )
                except Exception as e:
                    if "404" in str(e):
                        # create new batch stanza keeping old stanza as it is.
                        # property should be same as old
                        self.create_batch_stanza()

                reload_batch_input(self.getSessionKey())

        super(CustomConfigMigrationHandler, self).handleEdit(conf_info)


