class netskope(ModularInput):
    def _validate_arguments(self, val_data):
        """
        :param val_data: The data that requires validation.
        :return:
        RAISE an error if the arguments do not validate correctly. The default is just "True".
        """
        event_type_list = 'connection,alert,audit,application,web,clients,infrastructure'

        if len(val_data["tenanturl"]) > 255:
            raise Exception("TenantURL cannot be longer than 255 characters.")

        if "proxy_name" in val_data:
            if len(val_data["proxy_name"]) > 255:
                raise Exception("Proxy name cannot be longer than 255 characters.")
        try:
            limit_value = int(val_data["limit"])
        except:
            raise Exception("Limit must be numeric.")

        if limit_value > 5000:
            raise Exception("Limit cannot exceed 5000.")

        for evt_type in val_data["event_type"].split(","):
            if evt_type not in event_type_list:
                raise Exception("Event type: {} is invalid. Valid values are {}".format(evt_type, event_type_list))

        return True


